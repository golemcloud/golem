macro_rules! impl_tuple {
    ( $name:literal => $( $idx:tt : $t:ident ),+ ) => {
        impl< $( $t : IntoSchema ),+ > IntoSchema for ( $( $t, )+ ) {
            fn type_id() -> TypeId {
                type_id_with_args($name, &[ $( <$t as IntoSchema>::type_id() ),+ ])
            }
            fn register_in(b: &mut SchemaBuilder) -> SchemaType {
                SchemaType::Tuple {
                    elements: vec![ $( <$t as IntoSchema>::register_in(b) ),+ ],
                }
            }
            fn to_value(&self) -> SchemaValue {
                SchemaValue::Tuple {
                    elements: vec![ $( IntoSchema::to_value(&self.$idx) ),+ ],
                }
            }
        }
        impl< $( $t : FromSchema ),+ > FromSchema for ( $( $t, )+ ) {
            fn from_value(v: &SchemaValue) -> Result<Self, FromSchemaError> {
                match v {
                    SchemaValue::Tuple { elements } => {
                        let expected = [ $( stringify!($t) ),+ ].len();
                        if elements.len() != expected {
                            return Err(FromSchemaError::custom(format!(
                                "tuple length mismatch: expected {expected}, got {}",
                                elements.len()
                            )));
                        }
                        Ok(( $( <$t as FromSchema>::from_value(&elements[$idx])? , )+ ))
                    }
                    other => Err(FromSchemaError::shape_mismatch(
                        "tuple",
                        value_kind(other),
                        "tuple",
                    )),
                }
            }
        }
    };
}

