//! Runtime trait surface for converting Rust values to and from the
//! recursive schema model.
//!
//! The two core traits are:
//!
//! - [`IntoSchema`] — emits a self-contained [`SchemaGraph`] for a type and
//!   converts a Rust value of that type into a matching [`SchemaValue`].
//! - [`FromSchema`] — decodes a [`SchemaValue`] back into a Rust value.
//!
//! Hand-written impls for the standard primitive set live below. Derived
//! impls are generated by the `golem-schema-derive` crate, but the traits
//! themselves can be implemented by hand for any type that needs a custom
//! mapping.

use crate::schema::graph::{SchemaGraph, SchemaTypeDef};
use crate::schema::metadata::{MetadataEnvelope, TypeId};
use crate::schema::schema_type::SchemaType;
use crate::schema::schema_value::{
    BinaryValuePayload, DurationValuePayload, SchemaValue, SecretValuePayload, TextValuePayload,
};
use crate::schema::validation::{SchemaError, validate_graph};
use std::collections::{BTreeMap, HashMap, HashSet};
use std::fmt::{self, Display, Formatter};

// =====================================================================
// Trait surface
// =====================================================================

/// Convert a Rust type into a self-contained schema graph and a
/// [`SchemaValue`] tree.
///
/// The Rust-SDK default `type_id()` is derived from
/// [`core::any::type_name`] by replacing `::` with `.` so the identifier
/// matches the language-independent dotted-FQN form (`a.b.c.Xyz`, see
/// §4.20 of the value-type spec). Generic instantiations keep the angle
/// brackets and normalize their contents the same way:
/// `my_crate::Container<my_crate::user::Profile>` becomes
/// `my_crate.Container<my_crate.user.Profile>`.
///
/// `#[schema(named = "shared.foo")]` overrides the base name. For a
/// generic type the derive auto-suffixes the explicit name with the
/// resolved arguments so each instantiation gets its own definition.
pub trait IntoSchema {
    fn type_id() -> TypeId;
    fn register_in(builder: &mut SchemaBuilder) -> SchemaType;
    fn to_value(&self) -> SchemaValue;
}

/// Decode a [`SchemaValue`] back into a Rust value.
pub trait FromSchema: Sized {
    fn from_value(value: &SchemaValue) -> Result<Self, FromSchemaError>;
}

/// Build a self-contained graph for `T` and run well-formedness
/// validation on the result. Returns the first reported [`SchemaError`]
/// if the graph is malformed.
pub fn try_into_schema_graph<T: IntoSchema + ?Sized>() -> Result<SchemaGraph, SchemaError> {
    let mut builder = SchemaBuilder::new();
    let root = T::register_in(&mut builder);
    let graph = SchemaGraph {
        defs: builder.finish(),
        root,
    };
    match validate_graph(&graph) {
        Ok(()) => Ok(graph),
        Err(mut errors) => Err(errors.remove(0)),
    }
}

// =====================================================================
// Builder
// =====================================================================

/// Mutable working set used while a graph is being built up.
///
/// Derived `register_in` bodies call [`SchemaBuilder::reserve`] before
/// recursing into inner fields so back-references through
/// [`SchemaType::Ref`] can close without infinite recursion. Once the
/// body is fully constructed they call [`SchemaBuilder::commit`] to
/// promote the reservation to a real definition.
#[derive(Default)]
pub struct SchemaBuilder {
    defs: Vec<SchemaTypeDef>,
    reserved: HashSet<TypeId>,
}

impl SchemaBuilder {
    pub fn new() -> Self {
        Self::default()
    }

    /// `true` when `id` is already reserved or committed in this builder.
    pub fn is_registered(&self, id: &TypeId) -> bool {
        self.reserved.contains(id) || self.defs.iter().any(|d| &d.id == id)
    }

    /// Reserve a slot for `id` so recursive references close without
    /// infinite recursion. Must be followed by a matching
    /// [`Self::commit`] call once the body is built.
    pub fn reserve(&mut self, id: TypeId) {
        self.reserved.insert(id);
    }

    /// Promote a reservation to a real definition.
    pub fn commit(
        &mut self,
        id: TypeId,
        name: Option<String>,
        metadata: MetadataEnvelope,
        body: SchemaType,
    ) {
        self.reserved.remove(&id);
        // De-duplicate: if a definition with this id already exists
        // (because of a parallel registration in a sibling subtree) the
        // first one wins; later registrations are idempotent overwrites.
        if let Some(existing) = self.defs.iter_mut().find(|d| d.id == id) {
            *existing = SchemaTypeDef {
                id,
                name,
                metadata,
                body,
            };
        } else {
            self.defs.push(SchemaTypeDef {
                id,
                name,
                metadata,
                body,
            });
        }
    }

    /// Consume the builder and return the accumulated definitions.
    pub fn finish(self) -> Vec<SchemaTypeDef> {
        self.defs
    }

    /// Build a [`SchemaGraph`] using `root` as the entry point.
    pub fn into_graph(self, root: SchemaType) -> SchemaGraph {
        SchemaGraph {
            defs: self.finish(),
            root,
        }
    }
}

// =====================================================================
// TypeId helpers — used by generated code
// =====================================================================

/// Normalize a Rust path-form name into the dotted FQN used by [`TypeId`].
///
/// Every `::` becomes `.`; the inside of any `<…>` argument list is
/// normalized recursively by the same rule (because `::` only occurs in
/// path-like positions, a plain string replacement suffices). See §4.20.
pub fn normalize_type_path(raw: &str) -> String {
    raw.replace("::", ".")
}

/// Build a [`TypeId`] for a generic instantiation. `base` is the
/// already-normalized base name (e.g., `"my_crate.Container"`) and
/// `args` are the resolved argument [`TypeId`]s in declaration order.
///
/// `Container<u32>` → `my_crate.Container<u32>`.
/// `Container<u32, String>` → `my_crate.Container<u32, alloc.string.String>`.
pub fn type_id_with_args(base: &str, args: &[TypeId]) -> TypeId {
    if args.is_empty() {
        TypeId::new(base.to_string())
    } else {
        let mut s = String::with_capacity(base.len() + 16);
        s.push_str(base);
        s.push('<');
        for (i, a) in args.iter().enumerate() {
            if i > 0 {
                s.push_str(", ");
            }
            s.push_str(a.as_str());
        }
        s.push('>');
        TypeId::new(s)
    }
}

/// Default [`TypeId`] derivation from a `core::any::type_name::<Self>()`
/// output. Equivalent to `normalize_type_path` but exposed under a
/// generated-code-friendly name.
pub fn default_type_id_from(raw: &str) -> TypeId {
    TypeId::new(normalize_type_path(raw))
}

