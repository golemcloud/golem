impl IntoSchema for std::time::Duration {
    fn type_id() -> TypeId {
        TypeId::new("core.time.Duration")
    }
    fn register_in(_b: &mut SchemaBuilder) -> SchemaType {
        SchemaType::Duration
    }
    fn to_value(&self) -> SchemaValue {
        let nanos = self.as_nanos();
        let clamped: i64 = if nanos > i64::MAX as u128 {
            i64::MAX
        } else {
            nanos as i64
        };
        SchemaValue::Duration(DurationValuePayload {
            nanoseconds: clamped,
        })
    }
}

impl FromSchema for std::time::Duration {
    fn from_value(v: &SchemaValue) -> Result<Self, FromSchemaError> {
        match v {
            SchemaValue::Duration(p) => {
                if p.nanoseconds < 0 {
                    Err(FromSchemaError::custom(
                        "std::time::Duration cannot decode a negative duration",
                    ))
                } else {
                    Ok(std::time::Duration::from_nanos(p.nanoseconds as u64))
                }
            }
            other => Err(FromSchemaError::shape_mismatch(
                "duration",
                value_kind(other),
                "Duration",
            )),
        }
    }
}

impl IntoSchema for chrono::Duration {
    fn type_id() -> TypeId {
        TypeId::new("chrono.Duration")
    }
    fn register_in(_b: &mut SchemaBuilder) -> SchemaType {
        SchemaType::Duration
    }
    fn to_value(&self) -> SchemaValue {
        SchemaValue::Duration(DurationValuePayload {
            nanoseconds: self
                .num_nanoseconds()
                .unwrap_or(self.num_seconds() * 1_000_000_000),
        })
    }
}

impl FromSchema for chrono::Duration {
    fn from_value(v: &SchemaValue) -> Result<Self, FromSchemaError> {
        match v {
            SchemaValue::Duration(p) => Ok(chrono::Duration::nanoseconds(p.nanoseconds)),
            other => Err(FromSchemaError::shape_mismatch(
                "duration",
                value_kind(other),
                "chrono::Duration",
            )),
        }
    }
}
