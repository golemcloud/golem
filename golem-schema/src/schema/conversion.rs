macro_rules! impl_primitive {
    ($ty:ty, $type_id:literal, $schema_ty:expr, $variant:ident) => {
        impl IntoSchema for $ty {
            fn type_id() -> TypeId {
                TypeId::new($type_id)
            }
            fn register_in(_b: &mut SchemaBuilder) -> SchemaType {
                $schema_ty
            }
            fn to_value(&self) -> SchemaValue {
                SchemaValue::$variant(*self)
            }
        }
        impl FromSchema for $ty {
            fn from_value(v: &SchemaValue) -> Result<Self, FromSchemaError> {
                match v {
                    SchemaValue::$variant(x) => Ok(*x),
                    other => Err(FromSchemaError::shape_mismatch(
                        stringify!($variant),
                        value_kind(other),
                        stringify!($ty),
                    )),
                }
            }
        }
    };
}

impl_primitive!(bool, "bool", SchemaType::bool(), Bool);
impl_primitive!(i8, "i8", SchemaType::s8(), S8);
impl_primitive!(i16, "i16", SchemaType::s16(), S16);
impl_primitive!(i32, "i32", SchemaType::s32(), S32);
impl_primitive!(i64, "i64", SchemaType::s64(), S64);
impl_primitive!(u8, "u8", SchemaType::u8(), U8);
impl_primitive!(u16, "u16", SchemaType::u16(), U16);
impl_primitive!(u32, "u32", SchemaType::u32(), U32);
impl_primitive!(u64, "u64", SchemaType::u64(), U64);
impl_primitive!(f32, "f32", SchemaType::f32(), F32);
impl_primitive!(f64, "f64", SchemaType::f64(), F64);
impl_primitive!(char, "char", SchemaType::char(), Char);

impl IntoSchema for String {
    fn type_id() -> TypeId {
        TypeId::new("alloc.string.String")
    }
    fn register_in(_b: &mut SchemaBuilder) -> SchemaType {
        SchemaType::string()
    }
    fn to_value(&self) -> SchemaValue {
        SchemaValue::String(self.clone())
    }
}

impl FromSchema for String {
    fn from_value(v: &SchemaValue) -> Result<Self, FromSchemaError> {
        match v {
            SchemaValue::String(s) => Ok(s.clone()),
            other => Err(FromSchemaError::shape_mismatch(
                "string",
                value_kind(other),
                "String",
            )),
        }
    }
}

/// `str` schema is identical to [`String`], so a `&str` produces the same
/// schema graph and value as an owned `String`. This makes string literals
/// usable directly as agent parameters without an explicit `.to_string()`.
impl IntoSchema for str {
    fn type_id() -> TypeId {
        TypeId::new("alloc.string.String")
    }
    fn register_in(_b: &mut SchemaBuilder) -> SchemaType {
        SchemaType::string()
    }
    fn to_value(&self) -> SchemaValue {
        SchemaValue::String(self.to_string())
    }
}

/// Forward [`IntoSchema`] through shared references so that `&T` values (most
/// commonly `&str` string literals) can be used wherever an `IntoSchema` value
/// is expected.
impl<T: IntoSchema + ?Sized> IntoSchema for &T {
    fn type_id() -> TypeId {
        T::type_id()
    }
    fn register_in(b: &mut SchemaBuilder) -> SchemaType {
        T::register_in(b)
    }
    fn to_value(&self) -> SchemaValue {
        (**self).to_value()
    }
}

