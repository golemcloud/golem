/// Errors produced by [`FromSchema::from_value`].
#[derive(Clone, Debug, PartialEq, Eq)]
pub enum FromSchemaError {
    /// The expected value shape does not match what was provided.
    ShapeMismatch {
        expected: String,
        actual: String,
        context: String,
    },
    /// A variant or enum case index is out of range.
    OutOfRange {
        index: u32,
        len: u32,
        context: String,
    },
    /// A union tag was not matched by any branch.
    UnknownUnionTag(String),
    /// Free-form error message produced by generated decoders.
    Custom(String),
}

/// Backwards-compatible alias kept for codegen and consumers.
pub type DecodeError = FromSchemaError;

impl FromSchemaError {
    pub fn shape_mismatch(
        expected: impl Into<String>,
        actual: impl Into<String>,
        context: impl Into<String>,
    ) -> Self {
        Self::ShapeMismatch {
            expected: expected.into(),
            actual: actual.into(),
            context: context.into(),
        }
    }

    pub fn out_of_range(index: u32, len: u32, context: impl Into<String>) -> Self {
        Self::OutOfRange {
            index,
            len,
            context: context.into(),
        }
    }

    pub fn custom(msg: impl Into<String>) -> Self {
        Self::Custom(msg.into())
    }
}

impl Display for FromSchemaError {
    fn fmt(&self, f: &mut Formatter<'_>) -> fmt::Result {
        match self {
            FromSchemaError::ShapeMismatch {
                expected,
                actual,
                context,
            } => write!(
                f,
                "shape mismatch in {context}: expected {expected}, got {actual}"
            ),
            FromSchemaError::OutOfRange {
                index,
                len,
                context,
            } => write!(f, "index {index} out of range (len {len}) in {context}"),
            FromSchemaError::UnknownUnionTag(tag) => write!(f, "unknown union tag `{tag}`"),
            FromSchemaError::Custom(msg) => f.write_str(msg),
        }
    }
}

impl std::error::Error for FromSchemaError {}

