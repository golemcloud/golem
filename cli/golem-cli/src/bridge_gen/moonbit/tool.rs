#[derive(Clone)]
struct ClientNode {
    index: usize,
    struct_name: String,
    provided: Vec<CanonicalSurfaceRef>,
}

type CollectedNames = (
    BTreeMap<usize, ClientNode>,
    BTreeMap<usize, String>,
    Vec<String>,
);

/// Generates a MoonBit guest tool-client module.
pub struct MoonBitToolBridgeGenerator {
    tool: Tool,
    tool_name: String,
    target_path: Utf8PathBuf,
    inner: MoonBitBridgeGenerator,
    clients: BTreeMap<usize, ClientNode>,
    error_names: BTreeMap<usize, String>,
}

impl MoonBitToolBridgeGenerator {
    pub fn new(tool: Tool, target_path: &Utf8Path, testing: bool) -> anyhow::Result<Self> {
        let tool_name = tool
            .commands
            .nodes
            .first()
            .map(|node| node.name.clone())
            .ok_or_else(|| anyhow!("tool command tree must contain a root command"))?;
        let (clients, error_names, reserved) = collect_names(&tool, &tool_name)?;
        let mut synthetic = synthetic_agent_type(&tool, &tool_name)?;
        add_error_multimodal_collection_methods(&tool, &mut synthetic);
        let inner = MoonBitBridgeGenerator::new_guest_with_extra_reserved_names(
            synthetic,
            target_path,
            testing,
            reserved,
        )?;
        Ok(Self {
            tool,
            tool_name,
            target_path: target_path.to_path_buf(),
            inner,
            clients,
            error_names,
        })
    }

    pub fn generate(&mut self) -> anyhow::Result<()> {
        if !self.target_path.exists() {
            fs::create_dir_all(&self.target_path)?;
        }
        self.write_module_file()?;
        self.write_client()?;
        Ok(())
    }

    fn write_module_file(&self) -> anyhow::Result<()> {
        self.inner.write_module_file()?;
        let path = self.target_path.join("moon.mod.json");
        let mut manifest: serde_json::Value = serde_json::from_str(&fs::read_to_string(&path)?)
            .context("failed to parse generated MoonBit module manifest")?;
        manifest["name"] =
            serde_json::Value::String(tool_bridge_client_directory_name(&self.tool_name));
        fs::write_str(
            path,
            serde_json::to_string_pretty(&manifest)
                .context("failed to serialize generated MoonBit tool module manifest")?
                + "\n",
        )?;
        Ok(())
    }

    fn write_client(&mut self) -> anyhow::Result<()> {
        let client_dir = self.target_path.join("client");
        fs::create_dir_all(&client_dir)?;
        fs::write_str(
            client_dir.join("moon.pkg"),
            r#"import {
  "golemcloud/golem_sdk/interface/golem/core/types" @types,
  "golemcloud/golem_sdk/interface/wasi/io/streams",
  "golemcloud/golem_sdk/schema_model" @model,
  "golemcloud/golem_sdk/tool",
}
"#,
        )?;
        let content = self.generate_client_source()?;
        fs::write_str(client_dir.join("client.mbt"), content)?;
        Ok(())
    }

    fn generate_client_source(&mut self) -> anyhow::Result<String> {
        let mut writer = MoonBitWriter::new();
        writer.line("// Generated by golem-cli. Do not edit.");
        writer.blank();

        writer.line("fn drop_tool_stdout(stdout : @streams.OutputStream?) -> Unit {");
        writer.indent();
        writer.line("match stdout {");
        writer.indent();
        writer.line("Some(stream) => stream.drop()");
        writer.line("None => ()");
        writer.dedent();
        writer.line("}");
        writer.dedent();
        writer.line("}");
        writer.blank();

        self.client_items(&mut writer)?;
        self.error_items(&mut writer)?;
        self.inner.write_type_definitions(&mut writer)?;
        self.inner.write_guest_codec_support(&mut writer);
        self.inner.write_codecs(&mut writer)?;
        self.inner.write_multimodals(&mut writer)?;
        Ok(writer.finish())
    }

    fn client_items(&mut self, writer: &mut MoonBitWriter) -> anyhow::Result<()> {
        let clients = self.clients.values().cloned().collect::<Vec<_>>();
        for client in clients {
            let node = self.node(client.index)?.clone();
            write_doc(writer, &node);
            writer.line("///|");
            writer.line(format!("pub(all) struct {} {{", client.struct_name));
            writer.indent();
            writer.line("client : @tool.ToolClient");
            writer.line("inherited : Array[(String, @model.SchemaValue)]");
            writer.dedent();
            writer.line("}");
            writer.blank();

            if client.index == 0 {
                writer.line("///|");
                writer.line(format!(
                    "pub fn {}::new() -> {} {{",
                    client.struct_name, client.struct_name
                ));
                writer.indent();
                writer.line(format!(
                    "{{ client: @tool.ToolClient::new({}), inherited: [], }}",
                    moonbit_string_literal(&self.tool_name)
                ));
                writer.dedent();
                writer.line("}");
                writer.blank();
            }

            writer.line("///|");
            writer.line(format!(
                "pub fn {}::drop(self : {}) -> Unit {{",
                client.struct_name, client.struct_name
            ));
            writer.indent();
            writer.line("self.client.drop()");
            writer.dedent();
            writer.line("}");
            writer.blank();

            self.client_methods(writer, &client)?;
        }
        Ok(())
    }

    fn client_methods(
        &mut self,
        writer: &mut MoonBitWriter,
        client: &ClientNode,
    ) -> anyhow::Result<()> {
        let mut planned = Vec::new();
        let mut preferred = Vec::new();
        if self.node(client.index)?.body.is_some() {
            let name = if client.index == 0 {
                &self.tool_name
            } else {
                &self.node(client.index)?.name
            };
            planned.push((client.index, false));
            preferred.push(to_moonbit_term_ident(name, false));
        }
        for child_index in self.child_indices(client.index)? {
            let child = self.node(child_index)?;
            let has_subcommands = !child.subcommands.is_empty();
            if has_subcommands || child.body.is_some() {
                planned.push((child_index, has_subcommands));
                preferred.push(to_moonbit_term_ident(&child.name, false));
            }
        }
        let names = unique_idents_with_reserved(preferred, &["new", "drop"]);
        for ((index, accessor), name) in planned.into_iter().zip(names) {
            if accessor {
                self.accessor_method(writer, index, client, &name)?;
            } else {
                self.leaf_method(writer, index, client, &name)?;
            }
            writer.blank();
        }
        Ok(())
    }

    fn accessor_method(
        &mut self,
        writer: &mut MoonBitWriter,
        child_index: usize,
        parent: &ClientNode,
        method_name: &str,
    ) -> anyhow::Result<()> {
        let child_client = self
            .clients
            .get(&child_index)
            .cloned()
            .context("missing child client")?;
        let child = self.node(child_index)?.clone();
        let fields = global_surfaces(&self.tool, child_index)
            .into_iter()
            .filter(|surface| !parent.provided.contains(surface))
            .map(|surface| self.field(child_index, surface))
            .collect::<anyhow::Result<Vec<_>>>()?;
        let param_names = self.param_names(&fields);
        let params = self.param_decls(&fields, &param_names)?;

        write_doc(writer, &child);
        writer.line("///|");
        writer.line(format!(
            "pub fn {}::{}(self : {}{}) -> {} {{",
            parent.struct_name,
            method_name,
            parent.struct_name,
            format_params(&params),
            child_client.struct_name
        ));
        writer.indent();
        writer.line("let inherited = self.inherited.copy()");
        for (field, param) in fields.iter().zip(&param_names) {
            let encoded = guest_codec_source(
                self.inner
                    .encode_expr_with_multimodal(param, &field.type_)?,
            );
            writer.line(format!("let encoded = try {{ {encoded} }} catch{{"));
            writer.indent();
            writer.line(
                "error => abort(\"failed to encode inherited tool parameter: \" + repr(error))",
            );
            writer.dedent();
            writer.line("}");
            writer.line(format!(
                "inherited.push(({}, encoded))",
                moonbit_string_literal(&field.name)
            ));
        }
        writer.line(format!(
            "{{ client: @tool.ToolClient::new({}), inherited, }}",
            moonbit_string_literal(&self.tool_name)
        ));
        writer.dedent();
        writer.line("}");
        Ok(())
    }

    fn leaf_method(
        &mut self,
        writer: &mut MoonBitWriter,
        command_index: usize,
        client: &ClientNode,
        method_name: &str,
    ) -> anyhow::Result<()> {
        let node = self.node(command_index)?.clone();
        let body = node
            .body
            .as_ref()
            .context("leaf method needs command body")?;
        let (_, remaining_fields) = self.leaf_fields(command_index, &client.provided)?;
        let model = self.canonical_model_expr(command_index)?;
        let param_names = self.param_names(&remaining_fields);
        let mut params = self.param_decls(&remaining_fields, &param_names)?;
        let stdin = match &body.stdin {
            Some(stdin) if stdin.required => {
                params.push("stdin : @streams.InputStream".to_string());
                "Some(stdin)"
            }
            Some(_) => {
                params.push("stdin : @streams.InputStream?".to_string());
                "stdin"
            }
            None => "None",
        };
        let error_type = self.error_type(command_index, body);
        let success_type = self.success_type(body)?;

        write_doc(writer, &node);
        writer.line("///|");
        writer.line(format!(
            "pub fn {}::{}(self : {}{}) -> Result[{}, @tool.ToolError[{}]] {{",
            client.struct_name,
            method_name,
            client.struct_name,
            format_params(&params),
            success_type,
            error_type
        ));
        writer.indent();
        writer.line(format!("let model : @tool.CanonicalInputModel = {model}"));
        writer.line("let values = self.inherited.copy()");
        for (field, param) in remaining_fields.iter().zip(&param_names) {
            let encoded = guest_codec_source(
                self.inner
                    .encode_expr_with_multimodal(param, &field.type_)?,
            );
            writer.line(format!("let encoded = try {{ {encoded} }} catch{{"));
            writer.indent();
            writer.line(format!(
                "error => return Err(@tool.tool_protocol_error(\"failed to encode tool parameter {}: \" + repr(error)))",
                field.name.replace('"', "\\\"")
            ));
            writer.dedent();
            writer.line("}");
            writer.line(format!(
                "values.push(({}, encoded))",
                moonbit_string_literal(&field.name)
            ));
        }
        writer.line("let input = try {");
        writer.indent();
        writer.line("@tool.build_canonical_input(model, values)");
        writer.dedent();
        writer.line("} catch{");
        writer.indent();
        writer.line("error => return Err(@tool.tool_protocol_error(repr(error)))");
        writer.dedent();
        writer.line("}");

        let path = moonbit_string_array(
            &command_path(&self.tool, command_index)?
                .into_iter()
                .skip(1)
                .collect::<Vec<_>>(),
        );
        let invoke = if body.errors.is_empty() {
            format!("self.client.invoke_and_await_no_tool_error({path}, input, {stdin})")
        } else {
            let decoder = error_decoder_name(
                self.error_names
                    .get(&command_index)
                    .context("missing error enum")?,
            );
            format!("self.client.invoke_and_await({path}, input, {stdin}, {decoder})")
        };
        writer.line(format!("match {invoke} {{"));
        writer.indent();
        writer.line("Err(error) => Err(error)");
        writer.line("Ok(result) => {");
        writer.indent();
        self.result_decode(writer, body)?;
        writer.dedent();
        writer.line("}");
        writer.dedent();
        writer.line("}");
        writer.dedent();
        writer.line("}");
        Ok(())
    }

    fn result_decode(&self, writer: &mut MoonBitWriter, body: &CommandBody) -> anyhow::Result<()> {
        if let Some(result) = &body.result {
            writer.line("let typed = match @tool.expect_value(result.result) {");
            writer.indent();
            writer.line("Ok(value) => value");
            writer.line("Err(error) => {");
            writer.indent();
            writer.line("drop_tool_stdout(result.stdout)");
            writer.line("return Err(error)");
            writer.dedent();
            writer.line("}");
            writer.dedent();
            writer.line("}");
            let decoded = guest_codec_source(
                self.inner
                    .decode_expr_with_multimodal("typed.value", &result.type_)?,
            );
            writer.line(format!("let decoded = try {{ {decoded} }} catch{{"));
            writer.indent();
            writer.line("error => {");
            writer.indent();
            writer.line("drop_tool_stdout(result.stdout)");
            writer.line("return Err(@tool.tool_protocol_error(\"failed to decode tool result: \" + repr(error)))");
            writer.dedent();
            writer.line("}");
            writer.dedent();
            writer.line("}");
        } else {
            writer.line("match @tool.expect_no_value(result.result) {");
            writer.indent();
            writer.line("Err(error) => {");
            writer.indent();
            writer.line("drop_tool_stdout(result.stdout)");
            writer.line("return Err(error)");
            writer.dedent();
            writer.line("}");
            writer.line("Ok(_) => ()");
            writer.dedent();
            writer.line("}");
        }

        match (&body.result, &body.stdout) {
            (Some(_), Some(stdout)) if stdout.required => {
                writer.line("match @tool.expect_stdout(result.stdout) {");
                writer.indent();
                writer.line("Ok(stdout) => Ok((decoded, stdout))");
                writer.line("Err(error) => Err(error)");
                writer.dedent();
                writer.line("}");
            }
            (Some(_), Some(_)) => writer.line("Ok((decoded, result.stdout))"),
            (Some(_), None) => {
                write_drop_unexpected_stdout(writer);
                writer.line("Ok(decoded)");
            }
            (None, Some(stdout)) if stdout.required => {
                writer.line("@tool.expect_stdout(result.stdout)");
            }
            (None, Some(_)) => writer.line("Ok(result.stdout)"),
            (None, None) => {
                write_drop_unexpected_stdout(writer);
                writer.line("Ok(())");
            }
        }
        Ok(())
    }

    fn success_type(&self, body: &CommandBody) -> anyhow::Result<String> {
        let value = body
            .result
            .as_ref()
            .map(|result| self.inner.type_reference_with_multimodal(&result.type_))
            .transpose()?;
        let stdout = body.stdout.as_ref().map(|spec| {
            if spec.required {
                "@streams.OutputStream".to_string()
            } else {
                "@streams.OutputStream?".to_string()
            }
        });
        Ok(match (value, stdout) {
            (Some(value), Some(stdout)) => format!("({value}, {stdout})"),
            (Some(value), None) => value,
            (None, Some(stdout)) => stdout,
            (None, None) => "Unit".to_string(),
        })
    }

    fn error_items(&self, writer: &mut MoonBitWriter) -> anyhow::Result<()> {
        for (command_index, error_name) in &self.error_names {
            let body = self
                .node(*command_index)?
                .body
                .as_ref()
                .context("error enum command has no body")?;
            let variants = error_variant_names(body);
            writer.line("///|");
            writer.line(format!("pub(all) enum {error_name} {{"));
            writer.indent();
            for (case, variant) in body.errors.iter().zip(&variants) {
                if let Some(payload) = &case.payload {
                    writer.line(format!(
                        "{variant}({})",
                        self.inner.type_reference_with_multimodal(payload)?
                    ));
                } else {
                    writer.line(variant);
                }
            }
            writer.dedent();
            writer.line("} derive(Eq)");
            writer.blank();

            writer.line("///|");
            writer.line(format!(
                "fn {}(value : @model.TypedSchemaValue) -> Result[{error_name}, String] {{",
                error_decoder_name(error_name)
            ));
            writer.indent();
            for (case, variant) in body.errors.iter().zip(&variants) {
                let (typ, decode) = match &case.payload {
                    Some(payload) => (
                        self.inner.type_reference_with_multimodal(payload)?,
                        guest_codec_source(
                            self.inner
                                .decode_expr_with_multimodal("value.value", payload)?,
                        ),
                    ),
                    None => (
                        "Unit".to_string(),
                        guest_codec_source(self.inner.decode_expr(
                            "value.value",
                            &golem_common::schema::SchemaType::tuple(vec![]),
                            0,
                        )?),
                    ),
                };
                writer.line(format!("let decoded : {typ}? = try {{"));
                writer.indent();
                writer.line(format!("Some({decode})"));
                writer.dedent();
                writer.line("} catch{");
                writer.indent();
                writer.line("_ => None");
                writer.dedent();
                writer.line("}");
                writer.line("if decoded is Some(payload) {");
                writer.indent();
                if case.payload.is_some() {
                    writer.line(format!("return Ok({error_name}::{variant}(payload))"));
                } else {
                    writer.line(format!("return Ok({error_name}::{variant})"));
                }
                writer.dedent();
                writer.line("}");
            }
            writer.line("Err(\"remote tool error payload did not match any declared error case\")");
            writer.dedent();
            writer.line("}");
            writer.blank();
        }
        Ok(())
    }

    fn canonical_model_expr(&self, command_index: usize) -> anyhow::Result<String> {
        let model = self
            .tool
            .canonical_input_model(command_index)
            .map_err(|error| anyhow!("failed to build canonical tool input model: {error}"))?;
        let field_graphs = model
            .fields
            .iter()
            .map(|field| FieldGraph {
                graph: SchemaGraph {
                    defs: reachable_defs(&self.tool.schema, &field.type_),
                    root: field.type_.clone(),
                },
            })
            .collect::<Vec<_>>();
        let field_exprs = model
            .fields
            .iter()
            .zip(&field_graphs)
            .map(|(field, field_graph)| {
                format!(
                    "@tool.CanonicalInputField::{{ name: {}, aliases: {}, type_: {} }}",
                    moonbit_string_literal(&field.name),
                    moonbit_string_array(&field.aliases),
                    emit_schema_graph_literal(&field_graph.graph)
                )
            })
            .collect::<Vec<_>>()
            .join(", ");
        Ok(format!(
            "@tool.CanonicalInputModel::{{ fields: [{field_exprs}], record_graph: {} }}",
            emit_schema_graph_literal(&model.record_schema)
        ))
    }

    fn leaf_fields(
        &self,
        command_index: usize,
        provided: &[CanonicalSurfaceRef],
    ) -> anyhow::Result<(Vec<CanonicalInputField>, Vec<CanonicalInputField>)> {
        let provided_fields = provided
            .iter()
            .map(|surface| self.field(command_index, *surface))
            .collect::<anyhow::Result<Vec<_>>>()?;
        let provided_names = provided_fields
            .iter()
            .flat_map(field_names)
            .collect::<BTreeSet<_>>();
        let mut remaining = Vec::new();
        for surface in self.tool.canonical_input_surfaces(command_index) {
            let field = self.field(command_index, surface)?;
            if provided.contains(&surface)
                || field_names(&field)
                    .into_iter()
                    .any(|name| provided_names.contains(&name))
            {
                continue;
            }
            remaining.push(field);
        }
        Ok((provided_fields, remaining))
    }

    fn param_names(&self, fields: &[CanonicalInputField]) -> Vec<String> {
        let mut reserved = vec![
            "self",
            "stdin",
            "client",
            "inherited",
            "drop_tool_stdout",
            "encoded",
            "model",
            "values",
            "input",
            "result",
            "typed",
            "decoded",
            "stdout",
            "error",
            "value",
            "payload",
            "e0",
            "k0",
            "v0",
            "entries0",
            "m0",
            "entry0",
            "elems0",
            "tup0",
            "p0",
            "vp",
            "ub",
        ]
        .into_iter()
        .map(str::to_string)
        .collect::<Vec<_>>();
        for index in 0..64 {
            reserved.push(format!("te0_{index}"));
        }
        let reserved = reserved.iter().map(String::as_str).collect::<Vec<_>>();
        unique_idents_with_reserved(
            fields
                .iter()
                .map(|field| to_moonbit_term_ident(&field.name, false))
                .collect(),
            &reserved,
        )
    }

    fn param_decls(
        &self,
        fields: &[CanonicalInputField],
        names: &[String],
    ) -> anyhow::Result<Vec<String>> {
        fields
            .iter()
            .zip(names)
            .map(|(field, name)| {
                Ok(format!(
                    "{name} : {}",
                    self.inner.type_reference_with_multimodal(&field.type_)?
                ))
            })
            .collect()
    }

    fn error_type(&self, command_index: usize, body: &CommandBody) -> String {
        if body.errors.is_empty() {
            "@tool.NoToolError".to_string()
        } else {
            self.error_names
                .get(&command_index)
                .expect("error enum name")
                .clone()
        }
    }

    fn node(&self, index: usize) -> anyhow::Result<&CommandNode> {
        self.tool
            .commands
            .nodes
            .get(index)
            .ok_or_else(|| anyhow!("command node index {index} out of bounds"))
    }

    fn child_indices(&self, index: usize) -> anyhow::Result<Vec<usize>> {
        self.node(index)?
            .subcommands
            .iter()
            .map(|index| idx_to_usize(*index))
            .collect()
    }

    fn field(
        &self,
        command_index: usize,
        surface: CanonicalSurfaceRef,
    ) -> anyhow::Result<CanonicalInputField> {
        self.tool
            .canonical_field_for_surface(command_index, surface)
            .ok_or_else(|| {
                anyhow!("canonical surface {surface:?} did not resolve for command {command_index}")
            })
    }
}

fn add_error_multimodal_collection_methods(
    tool: &Tool,
    synthetic: &mut golem_common::schema::AgentTypeSchema,
) {
    let schemas = tool.commands.nodes.iter().flat_map(|node| {
        node.body
            .iter()
            .flat_map(|body| body.errors.iter().filter_map(|case| case.payload.clone()))
    });
    synthetic.methods.extend(
        schemas
            .enumerate()
            .map(|(index, schema)| AgentMethodSchema {
                name: format!("__tool_multimodal_{index}"),
                description: String::new(),
                prompt_hint: None,
                input_schema: InputSchema::parameters(vec![]),
                output_schema: OutputSchema::Single(Box::new(schema)),
                http_endpoint: vec![],
                read_only: None,
            }),
    );
}

struct FieldGraph {
    graph: SchemaGraph,
}

fn format_params(params: &[String]) -> String {
    if params.is_empty() {
        String::new()
    } else {
        format!(", {}", params.join(", "))
    }
}

fn moonbit_string_array(values: &[String]) -> String {
    format!(
        "[{}]",
        values
            .iter()
            .map(|value| moonbit_string_literal(value))
            .collect::<Vec<_>>()
            .join(", ")
    )
}

fn write_doc(writer: &mut MoonBitWriter, node: &CommandNode) {
    writer.doc(&node.doc.summary);
    if !node.doc.summary.is_empty() && !node.doc.description.is_empty() {
        writer.line("///");
    }
    writer.doc(&node.doc.description);
}

fn write_drop_unexpected_stdout(writer: &mut MoonBitWriter) {
    writer.line("match result.stdout {");
    writer.indent();
    writer.line("Some(stdout) => {");
    writer.indent();
    writer.line("stdout.drop()");
    writer.line("return Err(@tool.tool_protocol_error(\"tool result unexpectedly contained stdout stream\"))");
    writer.dedent();
    writer.line("}");
    writer.line("None => ()");
    writer.dedent();
    writer.line("}");
}

fn error_variant_names(body: &CommandBody) -> Vec<String> {
    unique_idents_with_reserved(
        body.errors
            .iter()
            .map(|case| to_moonbit_constructor_ident(&case.name, false))
            .collect(),
        &[],
    )
}

fn error_decoder_name(error_name: &str) -> String {
    format!("decode_{}_error", to_moonbit_term_ident(error_name, false))
}

fn collect_names(tool: &Tool, tool_name: &str) -> anyhow::Result<CollectedNames> {
    let mut clients = BTreeMap::new();
    let mut error_names = BTreeMap::new();
    let mut reserved = BTreeSet::new();
    let mut used = ["CodecError", "UnstructuredText", "UnstructuredBinary"]
        .into_iter()
        .map(str::to_string)
        .collect();
    collect_node_names(
        tool,
        tool_name,
        0,
        Vec::new(),
        Vec::new(),
        &mut clients,
        &mut error_names,
        &mut used,
        &mut reserved,
    )?;
    Ok((clients, error_names, reserved.into_iter().collect()))
}

#[allow(clippy::too_many_arguments)]
fn collect_node_names(
    tool: &Tool,
    tool_name: &str,
    index: usize,
    path: Vec<String>,
    provided: Vec<CanonicalSurfaceRef>,
    clients: &mut BTreeMap<usize, ClientNode>,
    error_names: &mut BTreeMap<usize, String>,
    used: &mut BTreeSet<String>,
    reserved: &mut BTreeSet<String>,
) -> anyhow::Result<()> {
    let node = tool
        .commands
        .nodes
        .get(index)
        .context("command index out of bounds")?;
    if !node.subcommands.is_empty() || index == 0 {
        let struct_name = unique_name(client_struct_name(tool_name, &path), used);
        reserved.insert(struct_name.clone());
        clients.insert(
            index,
            ClientNode {
                index,
                struct_name,
                provided: provided.clone(),
            },
        );
    }
    if node
        .body
        .as_ref()
        .is_some_and(|body| !body.errors.is_empty())
    {
        let error_name = unique_name(error_enum_name(tool_name, &path), used);
        reserved.insert(error_name.clone());
        error_names.insert(index, error_name);
    }
    for child_index in &node.subcommands {
        let child_index = idx_to_usize(*child_index)?;
        let child = tool
            .commands
            .nodes
            .get(child_index)
            .context("child command index out of bounds")?;
        let mut child_path = path.clone();
        child_path.push(child.name.clone());
        let mut child_provided = provided.clone();
        child_provided.extend(
            global_surfaces(tool, child_index)
                .into_iter()
                .filter(|surface| !provided.contains(surface)),
        );
        collect_node_names(
            tool,
            tool_name,
            child_index,
            child_path,
            child_provided,
            clients,
            error_names,
            used,
            reserved,
        )?;
    }
    Ok(())
}

fn client_struct_name(tool_name: &str, path: &[String]) -> String {
    projected_type_name(tool_name, path, "Client")
}

fn error_enum_name(tool_name: &str, path: &[String]) -> String {
    projected_type_name(tool_name, path, "Error")
}

fn projected_type_name(tool_name: &str, path: &[String], suffix: &str) -> String {
    let mut name = tool_name.to_upper_camel_case();
    for segment in path {
        name.push_str(&segment.to_upper_camel_case());
    }
    if name.is_empty() || !name.starts_with(|c: char| c.is_ascii_alphabetic()) {
        name = format!("Tool{name}");
    }
    name.push_str(suffix);
    name
}

fn unique_name(preferred: String, used: &mut BTreeSet<String>) -> String {
    if used.insert(preferred.clone()) {
        return preferred;
    }
    for suffix in 2.. {
        let candidate = format!("{preferred}{suffix}");
        if used.insert(candidate.clone()) {
            return candidate;
        }
    }
    unreachable!()
}

