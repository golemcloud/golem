    fn tool_generation_preserves_canonical_definition_order() {
        let a = TypeId::new("example.A");
        let b = TypeId::new("example.B");
        let mut root = node("ordered");
        root.body = Some(CommandBody {
            positionals: Positionals {
                fixed: vec![
                    positional("b", SchemaType::ref_to(b.clone())),
                    positional("a", SchemaType::ref_to(a.clone())),
                ],
                tail: None,
            },
            ..body()
        });
        let tool = Tool {
            version: "1".to_string(),
            commands: CommandTree { nodes: vec![root] },
            schema: SchemaGraph {
                defs: vec![
                    SchemaTypeDef {
                        id: a,
                        name: Some("A".to_string()),
                        body: SchemaType::string(),
                    },
                    SchemaTypeDef {
                        id: b,
                        name: Some("B".to_string()),
                        body: SchemaType::string(),
                    },
                ],
                root: SchemaType::record(vec![]),
            },
        };
        validate_tool(&tool).expect("tool with named positional types is valid");
        let canonical = tool
            .canonical_input_record_schema(0)
            .expect("valid tool has a canonical input record schema");
        let expected_graph = emit_schema_graph_literal(&canonical);

        let dir = tempfile::TempDir::new().unwrap();
        let target = Utf8Path::from_path(dir.path()).unwrap();
        let mut generator = MoonBitToolBridgeGenerator::new(tool, target, true).unwrap();
        generator.generate().unwrap();

        let source = std::fs::read_to_string(target.join("client/client.mbt")).unwrap();
        let model_start = source
            .find("let model : @tool.CanonicalInputModel")
            .unwrap();
        let model_end = source[model_start..].find("let values =").unwrap() + model_start;
        let generated_model = &source[model_start..model_end];
        assert!(
            generated_model.contains(&expected_graph),
            "generated canonical input graph differs from Tool::canonical_input_record_schema:\nexpected graph:\n{expected_graph}\ngenerated model:\n{generated_model}"
        );
    }

