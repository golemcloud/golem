const SDK: &str = "@golemcloud/golem-ts-sdk";

#[derive(Clone)]
struct ClientNode {
    index: usize,
    class_name: String,
    provided: Vec<CanonicalSurfaceRef>,
}

type CollectedNames = (
    BTreeMap<usize, ClientNode>,
    BTreeMap<usize, String>,
    Vec<String>,
);

pub struct TypeScriptToolBridgeGenerator {
    tool: Tool,
    tool_name: String,
    target_path: Utf8PathBuf,
    testing: bool,
    inner: TypeScriptBridgeGenerator,
    clients: BTreeMap<usize, ClientNode>,
    error_names: BTreeMap<usize, String>,
}

impl TypeScriptToolBridgeGenerator {
    pub fn new(tool: Tool, target_path: &Utf8Path, testing: bool) -> anyhow::Result<Self> {
        let tool_name = tool
            .commands
            .nodes
            .first()
            .context("tool command tree must contain a root command")?
            .name
            .clone();
        let (clients, error_names, reserved) = collect_names(&tool, &tool_name)?;
        let synthetic = synthetic_agent_type(&tool, &tool_name)?;
        let inner = TypeScriptBridgeGenerator::new_guest_with_extra_reserved_names(
            synthetic,
            target_path,
            testing,
            reserved,
        )?;
        Ok(Self {
            tool,
            tool_name,
            target_path: target_path.to_path_buf(),
            testing,
            inner,
            clients,
            error_names,
        })
    }

    pub fn generate(&mut self) -> anyhow::Result<()> {
        std::fs::create_dir_all(&self.target_path)?;
        let package_name = tool_bridge_client_directory_name(&self.tool_name);
        let dependency = TypeScriptBridgeGenerator::sdk_package_dep(self.testing)?;
        let package = json!({
            "name": package_name, "version": "0.0.1", "type": "module",
            "main": format!("{package_name}.js"), "types": format!("{package_name}.d.ts"),
            "scripts": { "build": "tsc" },
            "dependencies": { (SDK): dependency },
            "devDependencies": { "typescript": "^5.9", "@types/node": "^25" }
        });
        std::fs::write(
            self.target_path.join("package.json"),
            serde_json::to_string_pretty(&package)?,
        )?;
        let tsconfig = json!({ "compilerOptions": {
            "target": "es2020", "module": "esnext", "moduleResolution": "node",
            "strict": true, "declaration": true, "skipLibCheck": true
        }, "include": [format!("{package_name}.ts")] });
        std::fs::write(
            self.target_path.join("tsconfig.json"),
            serde_json::to_string_pretty(&tsconfig)?,
        )?;
        let source = self.source()?;
        std::fs::write(self.target_path.join(format!("{package_name}.ts")), source)?;
        Ok(())
    }

    fn source(&mut self) -> anyhow::Result<String> {
        let mut out = String::from("// Generated by golem-cli. Do not edit.\n");
        out.push_str("import { bridge as base } from '@golemcloud/golem-ts-sdk';\n");
        out.push_str("\ntype ToolInputStream = AsyncIterable<number>;\ntype ToolOutputStream = AsyncIterable<number>;\n\n");
        out.push_str("function protocol(context: string, error?: unknown): base.ToolRuntimeError<never> {\n  const detail = error instanceof Error ? `: ${error.message}` : '';\n  return { tag: 'rpc', error: { tag: 'protocol-error', val: `${context}${detail}` } };\n}\n\n");
        self.write_errors(&mut out)?;
        for client in self.clients.values().cloned().collect::<Vec<_>>() {
            self.write_client(&mut out, &client)?;
        }
        let mut types = TsWriter::new();
        self.inner.generate_ts_type_definitions(&mut types)?;
        out.push_str(&types.finish_string());
        Ok(out)
    }

    fn write_client(&mut self, out: &mut String, client: &ClientNode) -> anyhow::Result<()> {
        out.push_str(&format!("export class {} {{\n  constructor(private readonly runtime: base.ToolClientRuntime, private readonly inherited: base.SchemaValue[]) {{}}\n", client.class_name));
        if client.index == 0 {
            out.push_str(&format!("  static create(runtime: base.ToolClientRuntime = base.createToolClientRuntime({})): {} {{ return new {}(runtime, []); }}\n  static newClient(): {} {{ return this.create(); }}\n", q(&self.tool_name)?, client.class_name, client.class_name, client.class_name));
        }
        let mut member_names = ParameterNaming::new();
        member_names.reserve_many(["constructor", "create", "newClient", "runtime", "inherited"]);
        let mut planned = Vec::new();
        if self.node(client.index)?.body.is_some() {
            let preferred = if client.index == 0 {
                &self.tool_name
            } else {
                &self.node(client.index)?.name
            };
            planned.push((
                client.index,
                false,
                member_names.fresh(escape_js_ident(preferred)),
            ));
        }
        for child in self.child_indices(client.index)? {
            let node = self.node(child)?;
            if !node.subcommands.is_empty() || node.body.is_some() {
                planned.push((
                    child,
                    !node.subcommands.is_empty(),
                    member_names.fresh(escape_js_ident(&node.name)),
                ));
            }
        }
        for (index, accessor, name) in planned {
            if accessor {
                self.write_accessor(out, client, index, &name)?;
            } else {
                self.write_leaf(out, client, index, &name)?;
            }
        }
        out.push_str("}\n\n");
        Ok(())
    }

    fn write_accessor(
        &self,
        out: &mut String,
        parent: &ClientNode,
        child_index: usize,
        name: &str,
    ) -> anyhow::Result<()> {
        let child = self
            .clients
            .get(&child_index)
            .context("missing child client")?;
        let fields = global_surfaces(&self.tool, child_index)
            .into_iter()
            .filter(|s| !parent.provided.contains(s))
            .map(|s| self.field(child_index, s))
            .collect::<anyhow::Result<Vec<_>>>()?;
        let names = self.param_names(
            &fields,
            &["runtime", "inherited", "stdin", &child.class_name],
        );
        let params = self.params(&fields, &names)?;
        let encoded = fields
            .iter()
            .zip(&names)
            .map(|(f, n)| self.inner.encode_schema_value(n, &f.type_))
            .collect::<anyhow::Result<Vec<_>>>()?
            .join(", ");
        out.push_str(&format!(
            "  {name}({}): {} {{ return new {}(this.runtime, [...this.inherited, {encoded}]); }}\n",
            params.join(", "),
            child.class_name,
            child.class_name
        ));
        Ok(())
    }

    fn write_leaf(
        &self,
        out: &mut String,
        client: &ClientNode,
        index: usize,
        name: &str,
    ) -> anyhow::Result<()> {
        let body = self
            .node(index)?
            .body
            .as_ref()
            .context("leaf command has no body")?;
        let (provided, remaining) = self.leaf_fields(index, &client.provided)?;
        let mut all = provided;
        all.extend(remaining.clone());
        let graph = self.record_schema_json(&all)?;
        let error_type = self
            .error_names
            .get(&index)
            .map(String::as_str)
            .unwrap_or("never");
        let decode_error = format!("decode{error_type}");
        let names = self.param_names(
            &remaining,
            &["stdin", "runtime", "inherited", &decode_error],
        );
        let mut params = self.params(&remaining, &names)?;
        let stdin = match &body.stdin {
            Some(s) if s.required => {
                params.push("stdin: ToolInputStream".into());
                "stdin"
            }
            Some(_) => {
                params.push("stdin?: ToolInputStream".into());
                "stdin"
            }
            None => "undefined",
        };
        let return_type = self.return_type(body)?;
        let encoded = remaining
            .iter()
            .zip(&names)
            .map(|(f, n)| self.inner.encode_schema_value(n, &f.type_))
            .collect::<anyhow::Result<Vec<_>>>()?
            .join(", ");
        let path = serde_json::to_string(
            &command_path(&self.tool, index)?
                .into_iter()
                .skip(1)
                .collect::<Vec<_>>(),
        )?;
        out.push_str(&format!("  async {name}({}): Promise<{return_type}> {{\n    let typedInput: base.TypedSchemaValue;\n    try {{\n      const fields: base.SchemaValue[] = [...this.inherited, {encoded}];\n      typedInput = base.typedSchemaValueFromJson({}, {{ tag: 'record', fields }});\n    }} catch (error) {{ throw protocol('failed to encode tool input', error); }}\n    let invocation: base.ToolInvocationResult;\n    try {{ invocation = await this.runtime.invokeAndAwait({path}, typedInput, {stdin}); }} catch (error) {{\n      if (!base.isRpcError(error)) throw error;\n", params.join(", "), q(&graph)?));
        if body.errors.is_empty() {
            out.push_str(
                "      throw { tag: 'rpc', error } satisfies base.ToolRuntimeError<never>;\n",
            );
        } else {
            out.push_str(&format!("      try {{ throw base.splitToolRpcError(error, decode{error_type}); }} catch (decodeError) {{ if ((decodeError as any)?.tag === 'rpc' || (decodeError as any)?.tag === 'tool') throw decodeError; throw protocol('failed to decode declared tool error', decodeError); }}\n"));
        }
        out.push_str("    }\n    try {\n");
        self.write_result(out, body)?;
        out.push_str("    } catch (error) { base.disposeToolStdout(invocation.stdout); if ((error as any)?.tag === 'rpc') throw error; throw protocol('invalid tool result', error); }\n  }\n");
        Ok(())
    }

    fn write_result(&self, out: &mut String, body: &CommandBody) -> anyhow::Result<()> {
        if body.stdout.is_none() {
            out.push_str("      if (invocation.stdout !== undefined) throw new Error('tool result unexpectedly contained stdout stream');\n");
        }
        if body.result.is_none() {
            out.push_str("      if (invocation.result !== undefined) throw new Error('tool result unexpectedly contained a value');\n");
        }
        if body.stdout.as_ref().is_some_and(|s| s.required) {
            out.push_str("      if (invocation.stdout === undefined) throw new Error('tool result did not contain declared stdout stream');\n");
        }
        let decoded = if let Some(result) = &body.result {
            out.push_str("      if (invocation.result === undefined) throw new Error('tool result did not contain a value');\n");
            let graph = self.schema_graph_json(&result.type_)?;
            out.push_str(&format!(
                "      const expectedResultGraph = base.schemaGraphFromJson({});\n      if (!base.typedSchemaValueConforms(expectedResultGraph, invocation.result)) throw new Error('tool result schema or value does not conform to the declared result');\n",
                q(&graph)?
            ));
            Some(
                self.inner
                    .decode_schema_value("invocation.result.value", &result.type_)?,
            )
        } else {
            None
        };
        match (decoded, &body.stdout) {
            (Some(value), Some(stdout)) => out.push_str(&format!(
                "      return {{ result: {value}, stdout: invocation.stdout{} }};\n",
                if stdout.required { "!" } else { "" }
            )),
            (Some(value), None) => out.push_str(&format!("      return {value};\n")),
            (None, Some(stdout)) if stdout.required => {
                out.push_str("      return invocation.stdout!;\n")
            }
            (None, Some(_)) => out.push_str("      return invocation.stdout;\n"),
            (None, None) => out.push_str("      return;\n"),
        }
        Ok(())
    }

    fn write_errors(&self, out: &mut String) -> anyhow::Result<()> {
        for (&index, name) in &self.error_names {
            let body = self
                .node(index)?
                .body
                .as_ref()
                .context("error command has no body")?;
            let variants = error_variant_names(body);
            let arms = body
                .errors
                .iter()
                .zip(&variants)
                .map(|(case, variant)| {
                    Ok(match &case.payload {
                        Some(payload) => format!(
                            "{{ readonly tag: {}; readonly value: {} }}",
                            q(variant)?,
                            self.inner.type_reference(payload)?
                        ),
                        None => format!("{{ readonly tag: {} }}", q(variant)?),
                    })
                })
                .collect::<anyhow::Result<Vec<_>>>()?;
            out.push_str(&format!("export type {name} = {};\n", arms.join(" | ")));
            out.push_str(&format!(
                "function decode{name}(typed: base.TypedSchemaValue): {name} {{\n"
            ));
            for (case, variant) in body.errors.iter().zip(&variants) {
                let payload = case
                    .payload
                    .clone()
                    .unwrap_or_else(|| SchemaType::tuple(vec![]));
                let graph = self.schema_graph_json(&payload)?;
                let decoded = self.inner.decode_schema_value("typed.value", &payload)?;
                let result = if case.payload.is_some() {
                    format!("{{ tag: {}, value: {decoded} }}", q(variant)?)
                } else {
                    format!("{{ tag: {} }}", q(variant)?)
                };
                out.push_str(&format!(
                    "  {{ const expectedGraph = base.schemaGraphFromJson({}); if (base.typedSchemaValueConforms(expectedGraph, typed)) return {result}; }}\n",
                    q(&graph)?
                ));
            }
            out.push_str("  throw new Error('remote tool error payload did not match any declared error case');\n}\n\n");
        }
        Ok(())
    }

    fn return_type(&self, body: &CommandBody) -> anyhow::Result<String> {
        let stdout = |required| {
            if required {
                "ToolOutputStream"
            } else {
                "ToolOutputStream | undefined"
            }
        };
        Ok(match (&body.result, &body.stdout) {
            (Some(r), Some(s)) => format!(
                "{{ result: {}; stdout{}: ToolOutputStream }}",
                self.inner.type_reference(&r.type_)?,
                if s.required { "" } else { "?" }
            ),
            (Some(r), None) => self.inner.type_reference(&r.type_)?,
            (None, Some(s)) => stdout(s.required).into(),
            (None, None) => "void".into(),
        })
    }

    fn params(
        &self,
        fields: &[CanonicalInputField],
        names: &[String],
    ) -> anyhow::Result<Vec<String>> {
        fields
            .iter()
            .zip(names)
            .map(|(f, n)| Ok(format!("{n}: {}", self.inner.type_reference(&f.type_)?)))
            .collect()
    }

    fn param_names(&self, fields: &[CanonicalInputField], reserved: &[&str]) -> Vec<String> {
        let mut naming = ParameterNaming::new();
        naming.reserve_many(reserved.iter().copied());
        naming.reserve_many([
            "base",
            "protocol",
            "typedInput",
            "fields",
            "invocation",
            "error",
            "decodeError",
            "expectedResultGraph",
        ]);
        for (_, type_name) in self.inner.type_naming.types() {
            naming.reserve(format!("encode{type_name}"));
            naming.reserve(format!("decode{type_name}"));
        }
        fields
            .iter()
            .map(|field| naming.fresh(escape_js_ident(&field.name)))
            .collect()
    }

    fn leaf_fields(
        &self,
        index: usize,
        provided: &[CanonicalSurfaceRef],
    ) -> anyhow::Result<(Vec<CanonicalInputField>, Vec<CanonicalInputField>)> {
        let supplied = provided
            .iter()
            .map(|s| self.field(index, *s))
            .collect::<anyhow::Result<Vec<_>>>()?;
        let names = supplied
            .iter()
            .flat_map(field_names)
            .collect::<BTreeSet<_>>();
        let mut remaining = Vec::new();
        for surface in self.tool.canonical_input_surfaces(index) {
            let field = self.field(index, surface)?;
            if !provided.contains(&surface)
                && !field_names(&field).iter().any(|n| names.contains(n))
            {
                remaining.push(field);
            }
        }
        Ok((supplied, remaining))
    }
    fn record_schema_json(&self, fields: &[CanonicalInputField]) -> anyhow::Result<String> {
        let graphs = fields
            .iter()
            .map(|f| {
                (
                    f.name.clone(),
                    SchemaGraph {
                        defs: reachable_defs(&self.tool.schema, &f.type_),
                        root: f.type_.clone(),
                    },
                )
            })
            .collect::<Vec<_>>();
        let graph = record_schema_from_field_graphs(graphs.iter().map(|(n, g)| (n.as_str(), g)))
            .map_err(|e| anyhow!("failed to build tool input record schema: {e}"))?;
        self.serialize_schema_graph(graph)
    }
    fn schema_graph_json(&self, typ: &SchemaType) -> anyhow::Result<String> {
        self.serialize_schema_graph(SchemaGraph {
            defs: reachable_defs(&self.tool.schema, typ),
            root: typ.clone(),
        })
    }
    fn serialize_schema_graph(&self, graph: SchemaGraph) -> anyhow::Result<String> {
        let mut value = serde_json::to_value(graph)?;
        stringify_precision_sensitive_numbers(&mut value);
        Ok(serde_json::to_string(&value)?)
    }
    fn node(&self, i: usize) -> anyhow::Result<&CommandNode> {
        self.tool
            .commands
            .nodes
            .get(i)
            .ok_or_else(|| anyhow!("command node {i} out of bounds"))
    }
    fn child_indices(&self, i: usize) -> anyhow::Result<Vec<usize>> {
        self.node(i)?
            .subcommands
            .iter()
            .map(|i| idx_to_usize(*i))
            .collect()
    }
    fn field(&self, i: usize, s: CanonicalSurfaceRef) -> anyhow::Result<CanonicalInputField> {
        self.tool
            .canonical_field_for_surface(i, s)
            .ok_or_else(|| anyhow!("canonical surface {s:?} did not resolve"))
    }
}
fn q(s: &str) -> anyhow::Result<String> {
    Ok(serde_json::to_string(s)?)
}
fn unique(preferred: String, used: &mut BTreeSet<String>) -> String {
    let mut n = preferred.clone();
    let mut i = 1;
    while !used.insert(n.clone()) {
        n = format!("{preferred}{i}");
        i += 1;
    }
    n
}
fn type_ident(parts: impl IntoIterator<Item = String>, suffix: &str) -> String {
    let mut n = parts
        .into_iter()
        .map(|p| p.to_upper_camel_case())
        .collect::<String>();
    if n.is_empty() || !n.starts_with(|c: char| c.is_ascii_alphabetic()) {
        n = format!("Tool{n}");
    }
    format!("{}{suffix}", escape_js_ident(n))
}
fn error_variant_names(body: &CommandBody) -> Vec<String> {
    let mut n = ParameterNaming::new();
    body.errors
        .iter()
        .map(|e| n.fresh(type_ident([e.name.clone()], "")))
        .collect()
}
fn collect_names(tool: &Tool, name: &str) -> anyhow::Result<CollectedNames> {
    fn visit(
        tool: &Tool,
        tool_name: &str,
        i: usize,
        path: Vec<String>,
        provided: Vec<CanonicalSurfaceRef>,
        clients: &mut BTreeMap<usize, ClientNode>,
        errors: &mut BTreeMap<usize, String>,
        used: &mut BTreeSet<String>,
    ) -> anyhow::Result<()> {
        let node = tool
            .commands
            .nodes
            .get(i)
            .context("command index out of bounds")?;
        if i == 0 || !node.subcommands.is_empty() {
            let class_name = unique(
                type_ident(
                    std::iter::once(tool_name.to_string()).chain(path.clone()),
                    "Client",
                ),
                used,
            );
            clients.insert(
                i,
                ClientNode {
                    index: i,
                    class_name,
                    provided: provided.clone(),
                },
            );
        }
        if node.body.as_ref().is_some_and(|b| !b.errors.is_empty()) {
            errors.insert(
                i,
                unique(
                    type_ident(
                        std::iter::once(tool_name.to_string()).chain(path.clone()),
                        "Error",
                    ),
                    used,
                ),
            );
        }
        for child in &node.subcommands {
            let ci = idx_to_usize(*child)?;
            let cn = tool.commands.nodes.get(ci).context("child out of bounds")?;
            let mut p = path.clone();
            p.push(cn.name.clone());
            let mut supplied = provided.clone();
            supplied.extend(
                global_surfaces(tool, ci)
                    .into_iter()
                    .filter(|s| !provided.contains(s)),
            );
            visit(tool, tool_name, ci, p, supplied, clients, errors, used)?;
        }
        Ok(())
    }
    let mut c = BTreeMap::new();
    let mut e = BTreeMap::new();
    let mut u = BTreeSet::from([
        "protocol".to_string(),
        "ToolInputStream".to_string(),
        "ToolOutputStream".to_string(),
    ]);
    visit(tool, name, 0, vec![], vec![], &mut c, &mut e, &mut u)?;
    Ok((c, e, u.into_iter().collect()))
}
