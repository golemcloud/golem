#[derive(Clone)]
struct ClientNode {
    index: usize,
    struct_name: String,
    provided: Vec<CanonicalSurfaceRef>,
}

type CollectedNames = (
    BTreeMap<usize, ClientNode>,
    BTreeMap<usize, String>,
    Vec<String>,
);

/// Generates a Rust guest TOOL client crate.
pub struct RustToolBridgeGenerator {
    tool: Tool,
    tool_name: String,
    target_path: Utf8PathBuf,
    testing: bool,
    inner: RustBridgeGenerator,
    clients: BTreeMap<usize, ClientNode>,
    error_names: BTreeMap<usize, String>,
}

impl RustToolBridgeGenerator {
    pub fn new(tool: Tool, target_path: &Utf8Path, testing: bool) -> anyhow::Result<Self> {
        let tool_name = tool
            .commands
            .nodes
            .first()
            .map(|node| node.name.clone())
            .ok_or_else(|| anyhow!("tool command tree must contain a root command"))?;
        let (clients, error_names, reserved) = collect_names(&tool, &tool_name)?;
        let synthetic = synthetic_agent_type(&tool, &tool_name)?;
        let inner = RustBridgeGenerator::new_guest_with_extra_reserved_names(
            synthetic,
            target_path,
            testing,
            reserved,
        )?;
        Ok(Self {
            tool,
            tool_name,
            target_path: target_path.to_path_buf(),
            testing,
            inner,
            clients,
            error_names,
        })
    }

    pub fn generate(&mut self) -> anyhow::Result<()> {
        let cargo_toml_path = self.target_path.join("Cargo.toml");
        let lib_rs_path = self.target_path.join("src/lib.rs");

        if !self.target_path.exists() {
            std::fs::create_dir_all(&self.target_path)?;
        }
        let src_dir = self.target_path.join("src");
        if !src_dir.exists() {
            std::fs::create_dir_all(&src_dir)?;
        }

        self.generate_cargo_toml(&cargo_toml_path)?;
        self.generate_lib_rs(&lib_rs_path)?;
        Ok(())
    }

    fn generate_cargo_toml(&self, path: &Utf8Path) -> anyhow::Result<()> {
        let golem_source = if self.testing {
            GolemDependencySource::Path(workspace_root()?)
        } else {
            match sdk_overrides()?.golem_repo_root()? {
                Some(repo_root) => GolemDependencySource::Path(repo_root),
                None => GolemDependencySource::GitMain,
            }
        };

        let mut doc = DocumentMut::new();
        doc["package"] = Item::Table(Default::default());
        doc["package"]["name"] = value(tool_bridge_client_directory_name(&self.tool_name));
        doc["package"]["version"] = value("0.0.1");
        doc["package"]["edition"] = value("2021");
        doc["package"]["description"] = value("Generated by golem-cli");

        doc["dependencies"] = Item::Table(Table::default());
        doc["dependencies"]["chrono"] = dep("0.4", &[]);
        doc["dependencies"]["golem-rust"] =
            golem_source.dep_item("sdks/rust/golem-rust", &["export_golem_agentic", "macro"])?;
        doc["dependencies"]["serde"] = dep("1", &["derive"]);
        doc["dependencies"]["serde_json"] = dep("1", &[]);
        doc["dependencies"]["uuid"] = dep("1.18.1", &["v4"]);

        std::fs::write(path, doc.to_string())
            .map_err(|e| anyhow!("Failed to write Cargo.toml file: {e}"))?;
        Ok(())
    }

    fn generate_lib_rs(&mut self, path: &Utf8Path) -> anyhow::Result<()> {
        let tokens = self.generate_lib_rs_tokens()?;
        debug!("raw tool lib.rs:\n {}", tokens);
        let formatted = prettyplease::unparse(&syn::parse2(tokens)?);
        std::fs::write(path, formatted).map_err(|e| anyhow!("Failed to write lib.rs file: {e}"))?;
        Ok(())
    }

    fn generate_lib_rs_tokens(&mut self) -> anyhow::Result<TokenStream> {
        let runtime_prelude =
            RustRuntimeConfig::new(RustBridgeMode::GuestWasmRpc).generated_prelude();
        let client_items = self.client_items()?;
        let error_items = self.error_items()?;
        let type_definitions = self.inner.type_definitions()?;
        let multimodals = self.inner.multimodals()?;
        let languages = self.inner.languages_module();
        let mimetypes = self.inner.mimetypes_module();

        Ok(quote! {
            #![allow(unused)]
            #![allow(non_snake_case)]
            #![allow(clippy::all)]

            #runtime_prelude

            #(#client_items)*

            #(#error_items)*

            #type_definitions

            #multimodals

            #languages

            #mimetypes
        })
    }

    fn client_items(&mut self) -> anyhow::Result<Vec<TokenStream>> {
        let mut items = Vec::new();
        let clients = self.clients.values().cloned().collect::<Vec<_>>();
        for client in clients {
            let struct_ident = ident(&client.struct_name);
            let doc = self.doc_attrs(
                &self.node(client.index)?.doc.summary,
                &self.node(client.index)?.doc.description,
            );
            let new_method = if client.index == 0 {
                let tool_name = &self.tool_name;
                quote! {
                    pub fn new() -> Self {
                        Self {
                            rpc: golem_rust::golem_agentic::golem::tool::host::ToolRpc::new(#tool_name),
                            inherited: Vec::new(),
                        }
                    }
                }
            } else {
                quote! {}
            };
            let default_impl = if client.index == 0 {
                quote! {
                    impl Default for #struct_ident {
                        fn default() -> Self { Self::new() }
                    }
                }
            } else {
                quote! {}
            };
            let methods = self.client_methods(&client)?;
            items.push(quote! {
                #(#doc)*
                pub struct #struct_ident {
                    rpc: golem_rust::golem_agentic::golem::tool::host::ToolRpc,
                    inherited: Vec<crate::__golem_bridge_runtime::schema::SchemaValue>,
                }

                impl #struct_ident {
                    #new_method
                    #(#methods)*
                }

                #default_impl
            });
        }
        Ok(items)
    }

    fn client_methods(&mut self, client: &ClientNode) -> anyhow::Result<Vec<TokenStream>> {
        // Method names are uniquified within the client's impl block; the
        // root client reserves `new` for its generated constructor.
        let mut method_naming = ParameterNaming::new();
        if client.index == 0 {
            method_naming.reserve("new");
        }
        let mut method_ident = |generator: &Self, name: &str| {
            ident(method_naming.fresh(generator.inner.to_rust_ident(name)))
        };

        let mut planned = Vec::new();
        if self.node(client.index)?.body.is_some() {
            let name = if client.index == 0 {
                self.tool_name.clone()
            } else {
                self.node(client.index)?.name.clone()
            };
            planned.push((client.index, false, method_ident(self, &name)));
        }
        for child_index in self.child_indices(client.index)? {
            let child = self.node(child_index)?;
            let has_subcommands = !child.subcommands.is_empty();
            let has_body = child.body.is_some();
            if has_subcommands {
                planned.push((child_index, true, method_ident(self, &child.name)));
            } else if has_body {
                planned.push((child_index, false, method_ident(self, &child.name)));
            }
        }

        let mut methods = Vec::new();
        for (index, is_accessor, ident) in planned {
            if is_accessor {
                methods.push(self.accessor_method(index, client, ident)?);
            } else {
                methods.push(self.leaf_method(index, client, ident)?);
            }
        }
        Ok(methods)
    }

    fn accessor_method(
        &mut self,
        child_index: usize,
        parent: &ClientNode,
        method_ident: Ident,
    ) -> anyhow::Result<TokenStream> {
        let child_client = self
            .clients
            .get(&child_index)
            .cloned()
            .context("missing child client")?;
        let child = self.node(child_index)?;
        let child_struct = ident(&child_client.struct_name);
        let doc = self.doc_attrs(&child.doc.summary, &child.doc.description);
        let mut naming = ParameterNaming::new();
        naming.reserve_many(["self", "stdin"]);
        let mut param_defs = Vec::new();
        let mut encodes = Vec::new();
        for surface in global_surfaces(&self.tool, child_index)
            .into_iter()
            .filter(|surface| !parent.provided.contains(surface))
        {
            let field = self.field(child_index, surface)?;
            let param_name = naming.fresh(self.inner.to_rust_ident(&field.name));
            let param_ident = ident(&param_name);
            let param_type = self.inner.type_reference(&field.type_, false)?;
            let encode =
                self.inner
                    .emit_encode_expr(quote! { #param_ident }, &field.type_, false, 0)?;
            param_defs.push(quote! { #param_ident: #param_type });
            encodes.push(quote! {
                inherited.push((#encode).expect("failed to encode tool parameter"));
            });
        }
        let tool_name = &self.tool_name;
        Ok(quote! {
            #(#doc)*
            pub fn #method_ident(&self, #(#param_defs),*) -> #child_struct {
                let mut inherited = self.inherited.clone();
                #(#encodes)*
                #child_struct {
                    rpc: golem_rust::golem_agentic::golem::tool::host::ToolRpc::new(#tool_name),
                    inherited,
                }
            }
        })
    }

    fn leaf_method(
        &mut self,
        command_index: usize,
        client: &ClientNode,
        method_ident: Ident,
    ) -> anyhow::Result<TokenStream> {
        let node = self.node(command_index)?.clone();
        let body = node
            .body
            .as_ref()
            .context("leaf method needs command body")?;
        let doc = self.doc_attrs(&node.doc.summary, &node.doc.description);
        let (provided_fields, remaining_fields) =
            self.leaf_fields(command_index, &client.provided)?;
        let mut all_fields = provided_fields.clone();
        all_fields.extend(remaining_fields.clone());
        let schema_json = self.record_schema_json(&all_fields)?;

        let mut naming = ParameterNaming::new();
        naming.reserve_many(["self", "stdin"]);
        let mut param_defs = Vec::new();
        let mut field_encodes = Vec::new();
        for field in &remaining_fields {
            let param_name = naming.fresh(self.inner.to_rust_ident(&field.name));
            let param_ident = ident(&param_name);
            let typ = self.inner.type_reference(&field.type_, false)?;
            let encode =
                self.inner
                    .emit_encode_expr(quote! { #param_ident }, &field.type_, false, 0)?;
            let name = &field.name;
            param_defs.push(quote! { #param_ident: #typ });
            field_encodes.push(quote! {
                __fields.push((#encode).map_err(|e| golem_rust::agentic::tool_protocol_error(format!("failed to encode tool parameter `{}`: {e}", #name)))?);
            });
        }
        let stdin_expr = match &body.stdin {
            Some(stdin) if stdin.required => {
                param_defs.push(quote! { stdin: golem_rust::wasip2::io::streams::InputStream });
                quote! { Some(stdin) }
            }
            Some(_) => {
                param_defs
                    .push(quote! { stdin: Option<golem_rust::wasip2::io::streams::InputStream> });
                quote! { stdin }
            }
            None => quote! { None },
        };

        let path_tokens: Vec<_> = command_path(&self.tool, command_index)?
            .into_iter()
            .skip(1)
            .map(|segment| quote! { #segment.to_string() })
            .collect();
        let error_type = self.error_type(command_index, body);
        let return_type = self.return_type(body, &error_type)?;
        let invoke = self.invoke_expr(command_index, body, &stdin_expr, &path_tokens)?;
        let decode = self.result_decode(body)?;

        Ok(quote! {
            #(#doc)*
            pub async fn #method_ident(&self, #(#param_defs),*) -> #return_type {
                let __schema: golem_rust::SchemaGraph = serde_json::from_str(#schema_json)
                    .map_err(|e| golem_rust::agentic::tool_protocol_error(format!("failed to parse embedded tool input schema: {e}")))?;
                let mut __fields: Vec<crate::__golem_bridge_runtime::schema::SchemaValue> = self.inherited.clone();
                #(#field_encodes)*
                let __input = golem_rust::TypedSchemaValue::new(
                    __schema,
                    crate::__golem_bridge_runtime::schema::SchemaValue::Record { fields: __fields },
                );
                let __result = #invoke?;
                #decode
            }
        })
    }

    fn invoke_expr(
        &mut self,
        command_index: usize,
        body: &CommandBody,
        stdin_expr: &TokenStream,
        path_tokens: &[TokenStream],
    ) -> anyhow::Result<TokenStream> {
        if body.errors.is_empty() {
            Ok(quote! {
                golem_rust::agentic::invoke_and_await_infallible(
                    &self.rpc,
                    &[#(#path_tokens),*],
                    &__input,
                    #stdin_expr,
                )
            })
        } else {
            let error_ident = ident(
                self.error_names
                    .get(&command_index)
                    .context("missing error enum")?,
            );
            let decode_arms = self.error_decode_arms(&error_ident, body)?;
            Ok(quote! {
                golem_rust::agentic::invoke_and_await(
                    &self.rpc,
                    &[#(#path_tokens),*],
                    &__input,
                    #stdin_expr,
                    |__value: golem_rust::TypedSchemaValue| -> Result<#error_ident, String> {
                        let (_, __value) = __value.into_parts();
                        #(#decode_arms)*
                        Err("remote tool error payload did not match any declared error case".to_string())
                    },
                )
            })
        }
    }

    fn error_decode_arms(
        &mut self,
        error_ident: &Ident,
        body: &CommandBody,
    ) -> anyhow::Result<Vec<TokenStream>> {
        let mut arms = Vec::new();
        for (case, variant) in body.errors.iter().zip(error_variant_idents(body)) {
            if let Some(payload) = &case.payload {
                let dec =
                    self.inner
                        .emit_decode_expr(quote! { __value.clone() }, payload, false, 0)?;
                arms.push(quote! {
                    if let Ok(__payload) = (#dec) {
                        return Ok(#error_ident::#variant(__payload));
                    }
                });
            } else {
                arms.push(quote! {
                    if <() as golem_rust::FromSchema>::from_value(&__value).is_ok() {
                        return Ok(#error_ident::#variant);
                    }
                });
            }
        }
        Ok(arms)
    }

    fn result_decode(&mut self, body: &CommandBody) -> anyhow::Result<TokenStream> {
        match (&body.result, &body.stdout) {
            (Some(result), Some(_)) => {
                let dec =
                    self.inner
                        .emit_decode_expr(quote! { __value }, &result.type_, false, 0)?;
                Ok(quote! {
                    let __stdout = golem_rust::agentic::expect_stdout(__result.stdout)?;
                    let __value = golem_rust::agentic::expect_value(__result.result)?;
                    let (_, __value) = __value.into_parts();
                    let __decoded = (#dec).map_err(golem_rust::agentic::tool_protocol_error)?;
                    Ok((__decoded, __stdout))
                })
            }
            (Some(result), None) => {
                let dec =
                    self.inner
                        .emit_decode_expr(quote! { __value }, &result.type_, false, 0)?;
                Ok(quote! {
                    golem_rust::agentic::expect_no_stdout(__result.stdout)?;
                    let __value = golem_rust::agentic::expect_value(__result.result)?;
                    let (_, __value) = __value.into_parts();
                    (#dec).map_err(golem_rust::agentic::tool_protocol_error)
                })
            }
            (None, Some(_)) => Ok(quote! {
                let __stdout = golem_rust::agentic::expect_stdout(__result.stdout)?;
                golem_rust::agentic::expect_no_value(__result.result)?;
                Ok(__stdout)
            }),
            (None, None) => Ok(quote! {
                golem_rust::agentic::expect_no_stdout(__result.stdout)?;
                golem_rust::agentic::expect_no_value(__result.result)
            }),
        }
    }

    fn return_type(
        &mut self,
        body: &CommandBody,
        error_type: &TokenStream,
    ) -> anyhow::Result<TokenStream> {
        let stdout = quote! { golem_rust::wasip2::io::streams::OutputStream };
        let ok = match (&body.result, &body.stdout) {
            (Some(result), Some(_)) => {
                let result_type = self.inner.type_reference(&result.type_, false)?;
                quote! { (#result_type, #stdout) }
            }
            (Some(result), None) => self.inner.type_reference(&result.type_, false)?,
            (None, Some(_)) => stdout,
            (None, None) => quote! { () },
        };
        Ok(quote! { Result<#ok, golem_rust::agentic::ToolError<#error_type>> })
    }

    fn error_type(&self, command_index: usize, body: &CommandBody) -> TokenStream {
        if body.errors.is_empty() {
            quote! { std::convert::Infallible }
        } else {
            let name = self
                .error_names
                .get(&command_index)
                .expect("error enum name");
            let ident = ident(name);
            quote! { #ident }
        }
    }

    fn leaf_fields(
        &self,
        command_index: usize,
        provided: &[CanonicalSurfaceRef],
    ) -> anyhow::Result<(Vec<CanonicalInputField>, Vec<CanonicalInputField>)> {
        let provided_fields = provided
            .iter()
            .map(|surface| self.field(command_index, *surface))
            .collect::<anyhow::Result<Vec<_>>>()?;
        let provided_names = provided_fields
            .iter()
            .flat_map(field_names)
            .collect::<BTreeSet<_>>();
        let mut remaining = Vec::new();
        for surface in self.tool.canonical_input_surfaces(command_index) {
            let field = self.field(command_index, surface)?;
            if provided.contains(&surface)
                || field_names(&field)
                    .into_iter()
                    .any(|name| provided_names.contains(&name))
            {
                continue;
            }
            remaining.push(field);
        }
        Ok((provided_fields, remaining))
    }

    fn record_schema_json(&self, fields: &[CanonicalInputField]) -> anyhow::Result<String> {
        let field_graphs = fields
            .iter()
            .map(|field| FieldGraph {
                name: field.name.clone(),
                graph: SchemaGraph {
                    defs: reachable_defs(&self.tool.schema, &field.type_),
                    root: field.type_.clone(),
                },
            })
            .collect::<Vec<_>>();
        let schema = record_schema_from_field_graphs(
            field_graphs.iter().map(|f| (f.name.as_str(), &f.graph)),
        )
        .map_err(|e| anyhow!("failed to build tool input record schema: {e}"))?;
        serde_json::to_string(&schema).context("failed to serialize embedded tool input schema")
    }

    fn error_items(&mut self) -> anyhow::Result<Vec<TokenStream>> {
        let mut items = Vec::new();
        for (command_index, enum_name) in self.error_names.clone() {
            let node = self.node(command_index)?.clone();
            let body = node
                .body
                .as_ref()
                .context("error enum command has no body")?;
            let enum_ident = ident(&enum_name);
            let mut variants = Vec::new();
            let mut display_arms = Vec::new();
            for (case, variant) in body.errors.iter().zip(error_variant_idents(body)) {
                let name = &case.name;
                if let Some(payload) = &case.payload {
                    let typ = self.inner.type_reference(payload, false)?;
                    variants.push(quote! { #variant(#typ) });
                    display_arms.push(quote! { #enum_ident::#variant(payload) => write!(f, "{}: {:?}", #name, payload) });
                } else {
                    variants.push(quote! { #variant });
                    display_arms.push(quote! { #enum_ident::#variant => write!(f, "{}", #name) });
                }
            }
            items.push(quote! {
                #[derive(Debug, Clone)]
                pub enum #enum_ident {
                    #(#variants),*
                }

                impl std::fmt::Display for #enum_ident {
                    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
                        match self {
                            #(#display_arms),*
                        }
                    }
                }

                impl std::error::Error for #enum_ident {}
            });
        }
        Ok(items)
    }

    fn doc_attrs(&self, summary: &str, description: &str) -> Vec<TokenStream> {
        let mut docs = Vec::new();
        if !summary.is_empty() {
            docs.push(quote! { #[doc = #summary] });
        }
        if !description.is_empty() {
            docs.push(quote! { #[doc = ""] });
            docs.push(quote! { #[doc = #description] });
        }
        docs
    }

    fn node(&self, index: usize) -> anyhow::Result<&CommandNode> {
        self.tool
            .commands
            .nodes
            .get(index)
            .ok_or_else(|| anyhow!("command node index {index} out of bounds"))
    }

    fn child_indices(&self, index: usize) -> anyhow::Result<Vec<usize>> {
        self.node(index)?
            .subcommands
            .iter()
            .map(|idx| idx_to_usize(*idx))
            .collect()
    }

    fn field(
        &self,
        command_index: usize,
        surface: CanonicalSurfaceRef,
    ) -> anyhow::Result<CanonicalInputField> {
        self.tool
            .canonical_field_for_surface(command_index, surface)
            .ok_or_else(|| {
                anyhow!("canonical surface {surface:?} did not resolve for command {command_index}")
            })
    }
}

struct FieldGraph {
    name: String,
    graph: SchemaGraph,
}

fn collect_names(tool: &Tool, tool_name: &str) -> anyhow::Result<CollectedNames> {
    let mut clients = BTreeMap::new();
    let mut error_names = BTreeMap::new();
    let mut reserved = BTreeSet::new();
    let mut used = BTreeSet::new();
    collect_node_names(
        tool,
        tool_name,
        0,
        Vec::new(),
        Vec::new(),
        &mut clients,
        &mut error_names,
        &mut used,
        &mut reserved,
    )?;
    Ok((clients, error_names, reserved.into_iter().collect()))
}

#[allow(clippy::too_many_arguments)]
fn collect_node_names(
    tool: &Tool,
    tool_name: &str,
    index: usize,
    path: Vec<String>,
    provided: Vec<CanonicalSurfaceRef>,
    clients: &mut BTreeMap<usize, ClientNode>,
    error_names: &mut BTreeMap<usize, String>,
    used: &mut BTreeSet<String>,
    reserved: &mut BTreeSet<String>,
) -> anyhow::Result<()> {
    let node = tool
        .commands
        .nodes
        .get(index)
        .context("command index out of bounds")?;
    if !node.subcommands.is_empty() || index == 0 {
        let struct_name = unique_name(client_struct_name(tool_name, &path), used);
        reserved.insert(struct_name.clone());
        clients.insert(
            index,
            ClientNode {
                index,
                struct_name,
                provided: provided.clone(),
            },
        );
    }
    if node
        .body
        .as_ref()
        .is_some_and(|body| !body.errors.is_empty())
    {
        let error_name = unique_name(error_enum_name(tool_name, &path), used);
        reserved.insert(error_name.clone());
        error_names.insert(index, error_name);
    }
    for child_index in &node.subcommands {
        let child_index = idx_to_usize(*child_index)?;
        let mut child_path = path.clone();
        let child = tool
            .commands
            .nodes
            .get(child_index)
            .context("child command index out of bounds")?;
        child_path.push(child.name.clone());
        let mut child_provided = provided.clone();
        child_provided.extend(
            global_surfaces(tool, child_index)
                .into_iter()
                .filter(|surface| !provided.contains(surface)),
        );
        collect_node_names(
            tool,
            tool_name,
            child_index,
            child_path,
            child_provided,
            clients,
            error_names,
            used,
            reserved,
        )?;
    }
    Ok(())
}

fn synthetic_agent_type(tool: &Tool, tool_name: &str) -> anyhow::Result<AgentTypeSchema> {
    let root_doc = tool
        .commands
        .nodes
        .first()
        .map(|n| n.doc.summary.clone())
        .unwrap_or_default();
    let mut methods = Vec::new();
    for (index, node) in tool.commands.nodes.iter().enumerate() {
        let Some(body) = &node.body else { continue };
        let path = command_path(tool, index)?;
        let method_name = if index == 0 {
            tool_name.to_string()
        } else {
            path[1..].join("-")
        };
        let fields = tool
            .canonical_input_fields(index)
            .into_iter()
            .map(|field| NamedField::user_supplied(field.name, field.type_));
        methods.push(AgentMethodSchema {
            name: method_name.clone(),
            description: node.doc.summary.clone(),
            prompt_hint: None,
            input_schema: InputSchema::parameters(fields),
            output_schema: body
                .result
                .as_ref()
                .map(|result| OutputSchema::Single(Box::new(result.type_.clone())))
                .unwrap_or(OutputSchema::Unit),
            http_endpoint: vec![],
            read_only: None,
        });
        if !body.errors.is_empty() {
            methods.push(AgentMethodSchema {
                name: format!("{method_name}-errors"),
                description: String::new(),
                prompt_hint: None,
                input_schema: InputSchema::parameters(body.errors.iter().filter_map(|case| {
                    case.payload
                        .clone()
                        .map(|payload| NamedField::user_supplied(case.name.clone(), payload))
                })),
                output_schema: OutputSchema::Unit,
                http_endpoint: vec![],
                read_only: None,
            });
        }
    }
    Ok(AgentTypeSchema {
        type_name: AgentTypeName(tool_name.to_upper_camel_case()),
        description: root_doc,
        source_language: String::new(),
        schema: tool.schema.clone(),
        constructor: AgentConstructorSchema {
            name: None,
            description: String::new(),
            prompt_hint: None,
            input_schema: InputSchema::parameters(vec![]),
        },
        methods,
        dependencies: vec![],
        mode: AgentMode::Ephemeral,
        http_mount: None,
        snapshotting: Snapshotting::Disabled(Empty {}),
        config: vec![],
    })
}

fn client_struct_name(tool_name: &str, path: &[String]) -> String {
    let mut name = tool_name.to_upper_camel_case();
    for segment in path {
        name.push_str(&segment.to_upper_camel_case());
    }
    name.push_str("Client");
    name
}

fn error_enum_name(tool_name: &str, path: &[String]) -> String {
    let mut name = tool_name.to_upper_camel_case();
    for segment in path {
        name.push_str(&segment.to_upper_camel_case());
    }
    name.push_str("Error");
    name
}

fn unique_name(preferred: String, used: &mut BTreeSet<String>) -> String {
    if used.insert(preferred.clone()) {
        return preferred;
    }
    for suffix in 2.. {
        let candidate = format!("{preferred}{suffix}");
        if used.insert(candidate.clone()) {
            return candidate;
        }
    }
    unreachable!()
}

/// Rust enum variant identifiers for a body's error cases, in declaration
/// order. Names are uniquified after UpperCamelCase projection (distinct tool
/// identifiers may project to the same Rust name) and `Self` is reserved.
/// Both the enum definition and the decode arms use this single assignment so
/// they stay aligned.
fn error_variant_idents(body: &CommandBody) -> Vec<Ident> {
    let mut naming = ParameterNaming::new();
    naming.reserve("Self");
    body.errors
        .iter()
        .map(|case| ident(naming.fresh(case.name.to_upper_camel_case())))
        .collect()
}

fn global_surfaces(tool: &Tool, command_index: usize) -> Vec<CanonicalSurfaceRef> {
    tool.canonical_input_surfaces(command_index)
        .into_iter()
        .filter(|surface| {
            matches!(
                surface,
                CanonicalSurfaceRef::GlobalOption { .. } | CanonicalSurfaceRef::GlobalFlag { .. }
            )
        })
        .collect()
}

fn command_path(tool: &Tool, command_index: usize) -> anyhow::Result<Vec<String>> {
    fn visit(nodes: &[CommandNode], current: usize, target: usize, path: &mut Vec<String>) -> bool {
        path.push(nodes[current].name.clone());
        if current == target {
            return true;
        }
        for child in &nodes[current].subcommands {
            if let Some(child) = child.as_usize()
                && child < nodes.len()
                && visit(nodes, child, target, path)
            {
                return true;
            }
        }
        path.pop();
        false
    }
    let mut path = Vec::new();
    if !tool.commands.nodes.is_empty() && visit(&tool.commands.nodes, 0, command_index, &mut path) {
        Ok(path)
    } else {
        bail!("command node {command_index} is not reachable from root")
    }
}

fn idx_to_usize(index: CommandIndex) -> anyhow::Result<usize> {
    index
        .as_usize()
        .ok_or_else(|| anyhow!("negative command index {}", index.0))
}

fn field_names(field: &CanonicalInputField) -> Vec<String> {
    std::iter::once(field.name.clone())
        .chain(field.aliases.iter().cloned())
        .collect()
}

fn ident(name: impl AsRef<str>) -> Ident {
    Ident::new(name.as_ref(), Span::call_site())
}

