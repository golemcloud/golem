    @Test
    fun `resolves Datetime to datetime and Either to result`() {
        val datetime = SourceFile.kotlin(
            "Datetime.kt",
            """
            package cloud.golem
            data class Datetime(val seconds: Long, val nanoseconds: Int)
            """.trimIndent(),
        )
        val either = SourceFile.kotlin(
            "Either.kt",
            """
            package cloud.golem.runtime
            sealed class Either<out L, out R> {
                data class Left<out L>(val value: L) : Either<L, Nothing>()
                data class Right<out R>(val value: R) : Either<Nothing, R>()
            }
            """.trimIndent(),
        )
        val agent = SourceFile.kotlin(
            "EventAgent.kt",
            """
            package events

            import cloud.golem.annotations.Agent
            import cloud.golem.annotations.Endpoint
            import cloud.golem.BaseAgent
            import cloud.golem.Datetime
            import cloud.golem.runtime.Either

            @Agent(mount = "/events/{name}")
            class EventAgent(val name: String) : BaseAgent() {
                @Endpoint(post = "/at")
                fun at(t: Datetime): Either<String, Int> = Either.Right(t.nanoseconds)
            }
            """.trimIndent(),
        )
        val reg = generated(runProcessor(datetime, either, agent), "EventAgentRegistration.kt")
        // Datetime param -> "datetime"; Either<String,Int> return -> result<s32,string> (Right=ok).
        assertContains(reg, "NativeParamSchema(\"t\", \"datetime\")")
        assertContains(reg, "\"result<s32,string>\"")
        // param decode builds a Datetime; return encode branches on Either.
        assertContains(reg, "cloud.golem.Datetime(")
        assertContains(reg, "cloud.golem.runtime.Either.Right ->")
    }
