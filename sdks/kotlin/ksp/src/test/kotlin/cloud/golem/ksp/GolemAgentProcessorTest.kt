/**
 * Verifies the processor's GENERATED SOURCE TEXT and, for the dedicated compilation test, that
 * the generated registration + entry point compile against a JVM stub of the native SDK's
 * `cloud.golem.runtime` surface.
 *
 * Most tests use withCompilation=false because the generated code targets Kotlin/Wasm
 * (`kotlin.wasm.WasmExport`, which does not exist on the JVM classpath) and cannot be compiled by
 * the JVM test compiler as-is. The dedicated withCompilation=true test instead provides a
 * JVM-compatible stub of `cloud.golem.runtime` (NativeAgentDescriptor/SchemaValue/etc.) plus a
 * stub `@WasmExport` annotation, so compilation succeeds — this catches unresolved-helper
 * regressions that text-only assertions miss.
 */
class GolemAgentProcessorTest {

    private fun annotationStubs() = SourceFile.kotlin(
        "Annotations.kt",
        """
        package cloud.golem.annotations

        annotation class Agent(val mount: String = "", val description: String = "")
        annotation class Endpoint(
            val post: String = "",
            val get: String = "",
            val put: String = "",
            val delete: String = "",
            val path: String = ""
        )
        annotation class Prompt(val hint: String = "")
        annotation class Description(val text: String = "")
        """.trimIndent(),
    )

    private fun baseAgentStub() = SourceFile.kotlin(
        "BaseAgent.kt",
        """
        package cloud.golem
        abstract class BaseAgent
        interface Snapshotted<S> {
            var state: S
        }
        """.trimIndent(),
    )

    private fun runProcessor(vararg agents: SourceFile): KotlinCompilation {
        val compilation = KotlinCompilation().apply {
            sources = listOf(annotationStubs(), baseAgentStub(), *agents)
            inheritClassPath = true
            messageOutputStream = System.out
        }
        compilation.configureKsp {
            symbolProcessorProviders += GolemAgentProcessorProvider()
            // Run KSP but do NOT compile the generated sources: the generated registration
            // targets Kotlin/Wasm (kotlin.wasm.WasmExport) and won't compile on the JVM test
            // compiler without stubs. We assert on the generated source text instead.
            withCompilation = false
        }
        compilation.compile()
        return compilation
    }

    private fun generated(compilation: KotlinCompilation, fileName: String): String {
        val file = compilation.kspSourcesDir.walkTopDown().firstOrNull { it.name == fileName }
        assertNotNull(file, "$fileName was not generated under ${compilation.kspSourcesDir}")
        return file.readText()
    }

    private fun generatedWit(compilation: KotlinCompilation): File? = compilation.kspSourcesDir.walkTopDown().firstOrNull { it.extension == "wit" }

    @Test
    fun `generates native Registration, entry point, and WIT for CounterAgent`() {
        val agent = SourceFile.kotlin(
            "CounterAgent.kt",
            """
            package counter

            import cloud.golem.annotations.Agent
            import cloud.golem.annotations.Endpoint
            import cloud.golem.annotations.Prompt
            import cloud.golem.annotations.Description
            import cloud.golem.BaseAgent

            @Agent(mount = "/counters/{name}", description = "A durable counter agent")
            class CounterAgent(val name: String) : BaseAgent() {
                private var value = 0

                @Prompt("Increase the count by one")
                @Endpoint(post = "/increment")
                fun increment(): Int { value++; return value }

                @Prompt("Get the current count")
                @Endpoint(get = "/value")
                fun getValue(): Int = value
            }
            """.trimIndent(),
        )
        val compilation = runProcessor(agent)

        val reg = generated(compilation, "CounterAgentRegistration.kt")
        assertContains(reg, "AUTO-GENERATED by golem-kotlin-ksp")
        assertContains(reg, "fun registerCounterAgent()")
        assertContains(reg, "import cloud.golem.runtime.NativeAgentRuntime")
        assertContains(reg, "NativeAgentRuntime.registerAgent(")
        assertContains(reg, "NativeAgentDescriptor(")
        assertContains(reg, "\"CounterAgent\"")
        assertContains(reg, "\"A durable counter agent\"")
        assertContains(reg, "\"/counters/{name}\"")
        assertContains(reg, "listOf(NativeParamSchema(\"name\", \"string\"))")
        assertContains(reg, "NativeMethodDescriptor(\"increment\", \"s32\", emptyList(), listOf(NativeHttpEndpoint(\"POST\", \"/increment\", false, emptyList())), \"Increase the count by one\", null)")
        assertContains(reg, "SchemaValue.S32((instance as CounterAgent).increment())")
        assertContains(reg, "SchemaValue.S32((instance as CounterAgent).getValue())")
        assertContains(reg, "CounterAgent((input[0] as SchemaValue.Str).v)")

        val entry = generated(compilation, "GolemGeneratedGuest.kt")
        assertContains(entry, "fun registerAllAgents()")
        assertContains(entry, "counter.registerCounterAgent()")
        assertContains(entry, "@WasmExport(\"golem:agent/guest@2.0.0#initialize\")")
        assertContains(entry, "@WasmExport(\"golem:agent/guest@2.0.0#invoke\")")
        assertContains(entry, "@WasmExport(\"golem:agent/guest@2.0.0#get-definition\")")
        assertContains(entry, "@WasmExport(\"golem:agent/guest@2.0.0#discover-agent-types\")")
        assertContains(entry, "@WasmExport(\"cabi_post_golem:agent/guest@2.0.0#initialize\")")
        assertContains(entry, "cloud.golem.runtime.initialize(argsPtr)")
        assertContains(entry, "@WasmExport(\"golem:api/save-snapshot@1.5.0#save\")")
        assertContains(entry, "@WasmExport(\"cabi_post_golem:api/save-snapshot@1.5.0#save\")")
        assertContains(entry, "@WasmExport(\"golem:api/load-snapshot@1.5.0#load\")")
        assertContains(entry, "@WasmExport(\"cabi_post_golem:api/load-snapshot@1.5.0#load\")")
        assertContains(entry, "cloud.golem.runtime.saveSnapshot()")
        assertContains(entry, "cloud.golem.runtime.cabiPostSaveSnapshot(resultPtr)")
        assertContains(entry, "fun golemLoadSnapshot(payloadPtr: Int, payloadLen: Int, mimeTypePtr: Int, mimeTypeLen: Int): Int")
        assertContains(entry, "cloud.golem.runtime.loadSnapshotFlat(payloadPtr, payloadLen, mimeTypePtr, mimeTypeLen)")
        assertContains(entry, "cloud.golem.runtime.cabiPostLoadSnapshot(resultPtr)")

        val wit = generatedWit(compilation)
        assertNotNull(wit, "counter-agent.wit was not generated")
        val witSrc = wit.readText()
        assertContains(witSrc, "package kotlin:counter@0.1.0")
        assertContains(witSrc, "world counter-agent {")
        assertContains(witSrc, "include golem:agent/agent-guest@2.0.0;")
        assertContains(witSrc, "// agent CounterAgent {")
        assertContains(witSrc, "//   mount: \"/counters/{name}\"")
        assertContains(witSrc, "//   constructor(name: string)")
        assertContains(witSrc, "//   method increment() -> s32")
        assertContains(witSrc, "//   method getValue() -> s32")
        assertContains(witSrc, "post: \"/increment\"")
        assertContains(witSrc, "prompt: \"Increase the count by one\"")
    }

