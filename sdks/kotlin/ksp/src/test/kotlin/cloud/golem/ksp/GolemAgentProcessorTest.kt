    // Proves the generated registration + entry point actually compile against a matching
    // native-runtime boundary (JVM-stubbed). Would catch unresolved-helper regressions.
    // Only the Kotlin language itself may declare anything in the `kotlin.*` package (the
    // compiler rejects user code doing so), so `kotlin.wasm.WasmExport` cannot be stubbed for a
    // JVM test -- meaning GolemGeneratedGuest.kt (which references it) can never compile on the
    // JVM test compiler, structurally. So: run KSP once (withCompilation=false) to capture the
    // generated REGISTRATION file's text, then compile ONLY that captured source (a plain
    // second pass, no KSP, no @WasmExport-referencing file involved) against a JVM stub of
    // cloud.golem.runtime. This still catches unresolved-helper regressions in the emitter.
    @Test
    fun `generated registration compiles against JVM stub native runtime`() {
        val sdkStub = SourceFile.kotlin(
            "GolemRuntimeStub.kt",
            """
            package cloud.golem.runtime

            // JVM-compilable stub mirroring the native SDK's cloud.golem.runtime surface the
            // generated registration code uses (NOT the @WasmExport entry point -- see above).
            sealed class SchemaValue {
                data class S32(val v: Int) : SchemaValue()
                data class Str(val v: String) : SchemaValue()
                data class Record(val fields: List<SchemaValue>) : SchemaValue()
                object Unit_ : SchemaValue()
            }

            class NativeParamSchema(val name: String, val witType: String)
            class NativeHttpEndpoint(
                val verb: String,
                val path: String,
                val auth: Boolean = false,
                val cors: List<String> = emptyList()
            )
            class NativeMethodDescriptor(
                val name: String,
                val outputWitType: String,
                val inputParams: List<NativeParamSchema>,
                val httpEndpoints: List<NativeHttpEndpoint>,
                val promptHint: String = "",
                val readOnlyCache: String? = null,
                val handler: (Any, List<SchemaValue>) -> SchemaValue
            )
            class NativeAgentDescriptor(
                val typeName: String,
                val description: String,
                val mountPath: String,
                val constructorParams: List<NativeParamSchema>,
                val methods: List<NativeMethodDescriptor>,
                val factory: (List<SchemaValue>) -> Any,
                val mountAuth: Boolean = false,
                val mountCors: List<String> = emptyList(),
                val mode: String = "durable",
                val snapshotting: String = "disabled"
            )
            object NativeAgentRuntime {
                fun registerAgent(descriptor: NativeAgentDescriptor) {}
            }
            """.trimIndent(),
        )

        val agent = SourceFile.kotlin(
            "CounterAgentCompile.kt",
            """
            package counter

            import cloud.golem.annotations.Agent
            import cloud.golem.annotations.Endpoint
            import cloud.golem.BaseAgent

            @Agent(mount = "/counters/{name}", description = "A durable counter agent")
            class CounterAgentCompile(val name: String) : BaseAgent() {
                private var value = 0

                @Endpoint(post = "/increment")
                fun increment(): Int { value++; return value }
            }
            """.trimIndent(),
        )

        // Pass 1: run KSP (no compilation) to capture the generated registration file's text.
        val kspCompilation = runProcessor(agent)
        val registrationSource = generated(kspCompilation, "CounterAgentCompileRegistration.kt")

        // Pass 2: compile the ORIGINAL agent source + the captured registration text + the JVM
        // stub together (plain compilation, no KSP, no @WasmExport-referencing file involved).
        val compilation = KotlinCompilation().apply {
            sources = listOf(
                annotationStubs(),
                baseAgentStub(),
                sdkStub,
                agent,
                SourceFile.kotlin("CounterAgentCompileRegistration.kt", registrationSource),
            )
            inheritClassPath = true
            messageOutputStream = System.out
        }
        val result = compilation.compile()
        assertTrue(
            result.exitCode == KotlinCompilation.ExitCode.OK,
            "Generated registration did not compile: ${result.messages}",
        )
    }

    @Test
    fun `derives SnapshotCodec for an agent implementing Snapshotted with a data class state`() {
        val state = SourceFile.kotlin(
            "CounterState.kt",
            """
            package snap
            data class CounterState(val value: Int, val label: String)
            """.trimIndent(),
        )
        val agent = SourceFile.kotlin(
            "SnapAgent.kt",
            """
            package snap

            import cloud.golem.annotations.Agent
            import cloud.golem.annotations.Endpoint
            import cloud.golem.BaseAgent
            import cloud.golem.Snapshotted

            @Agent(mount = "/snap")
            class SnapAgent : BaseAgent(), Snapshotted<CounterState> {
                override var state = CounterState(0, "")

                @Endpoint(post = "/inc")
                fun inc(): Int { state = state.copy(value = state.value + 1); return state.value }
            }
            """.trimIndent(),
        )
        val reg = generated(runProcessor(state, agent), "SnapAgentRegistration.kt")
        // The snapshot codec argument is spliced after the snapshotting argument.
        assertContains(reg, "snapshotCodec = cloud.golem.runtime.SnapshotCodec(")
        assertContains(reg, "save = { inst -> cloud.golem.runtime.SchemaValueBytes.encode(")
        assertContains(reg, "load = { inst, bytes -> (inst as SnapAgent).state =")
        // Round-trips through the record encoder for the data-class state.
        assertContains(reg, "(inst as SnapAgent).state")
        assertContains(reg, "cloud.golem.runtime.SchemaValueBytes.decode(bytes)")
    }

    // Proves the derived SnapshotCodec compiles against a JVM stub of the native runtime surface
    // (mirrors the registration-compile test, extended with SnapshotCodec/SchemaValueBytes and the
    // snapshotCodec descriptor param). Catches unresolved-helper / mis-spliced-expression regressions
    // in the generated save/load bodies that text-only assertions miss.
    @Test
    fun `derived SnapshotCodec compiles against JVM stub native runtime`() {
        val sdkStub = SourceFile.kotlin(
            "GolemRuntimeStubSnap.kt",
            """
            package cloud.golem.runtime

            sealed class SchemaValue {
                data class S32(val v: Int) : SchemaValue()
                data class Str(val v: String) : SchemaValue()
                data class Record(val fields: List<SchemaValue>) : SchemaValue()
                object Unit_ : SchemaValue()
            }

            object SchemaValueBytes {
                fun encode(v: SchemaValue): ByteArray = ByteArray(0)
                fun decode(bytes: ByteArray): SchemaValue = SchemaValue.Unit_
            }
            class SnapshotCodec(
                val save: (Any) -> ByteArray,
                val load: (Any, ByteArray) -> Unit,
            )

            class NativeParamSchema(val name: String, val witType: String)
            class NativeHttpEndpoint(
                val verb: String,
                val path: String,
                val auth: Boolean = false,
                val cors: List<String> = emptyList()
            )
            class NativeMethodDescriptor(
                val name: String,
                val outputWitType: String,
                val inputParams: List<NativeParamSchema>,
                val httpEndpoints: List<NativeHttpEndpoint>,
                val promptHint: String = "",
                val readOnlyCache: String? = null,
                val handler: (Any, List<SchemaValue>) -> SchemaValue
            )
            class NativeAgentDescriptor(
                val typeName: String,
                val description: String,
                val mountPath: String,
                val constructorParams: List<NativeParamSchema>,
                val methods: List<NativeMethodDescriptor>,
                val factory: (List<SchemaValue>) -> Any,
                val mountAuth: Boolean = false,
                val mountCors: List<String> = emptyList(),
                val mode: String = "durable",
                val snapshotting: String = "disabled",
                val snapshotCodec: SnapshotCodec? = null
            )
            object NativeAgentRuntime {
                fun registerAgent(descriptor: NativeAgentDescriptor) {}
            }
            """.trimIndent(),
        )
        val state = SourceFile.kotlin(
            "CounterStateCompile.kt",
            """
            package snap
            data class CounterStateCompile(val value: Int, val label: String)
            """.trimIndent(),
        )
        val agent = SourceFile.kotlin(
            "SnapAgentCompile.kt",
            """
            package snap

            import cloud.golem.annotations.Agent
            import cloud.golem.annotations.Endpoint
            import cloud.golem.BaseAgent
            import cloud.golem.Snapshotted

            @Agent(mount = "/snap")
            class SnapAgentCompile : BaseAgent(), Snapshotted<CounterStateCompile> {
                override var state = CounterStateCompile(0, "")

                @Endpoint(post = "/inc")
                fun inc(): Int = state.value
            }
            """.trimIndent(),
        )

        // Pass 1: run KSP (no compilation) to capture the generated registration file's text.
        val kspCompilation = runProcessor(state, agent)
        val registrationSource = generated(kspCompilation, "SnapAgentCompileRegistration.kt")

        // Pass 2: compile the agent + captured registration + JVM stub together (no KSP).
        val compilation = KotlinCompilation().apply {
            sources = listOf(
                annotationStubs(),
                baseAgentStub(),
                sdkStub,
                state,
                agent,
                SourceFile.kotlin("SnapAgentCompileRegistration.kt", registrationSource),
            )
            inheritClassPath = true
            messageOutputStream = System.out
        }
        val result = compilation.compile()
        assertTrue(
            result.exitCode == KotlinCompilation.ExitCode.OK,
            "Generated SnapshotCodec did not compile: ${result.messages}",
        )
    }

